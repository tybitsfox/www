有256个中断或异常向量，所以IDT的表项也就256个，每一个中断向量对应一个描述符。所以我们通过中断描述符IDT就可以得到我们的中断服务函数的地址，而IDT表的基地址则是存放在IDTR寄存器中的，通过 IDTR 寄存器找到 IDT 表，在通过 IDT表查到中断服
