<font color=red>【x86架构】APIC -- 高级可编程中断控制器</font>

x86架构的中断 类 型以及实现方式上有很多种。从中断控制器模块上分，x86中有8259中断控制器、Local APIC 以及 I/O APIC ，另外在PCI/PCIE中的存在MSI中断。从类型上来分，有硬件中断和软件中断之分，有可屏蔽中断和不可屏蔽中断之分。这部分的分类，
前者是按照中断源来分的，可以是软件主动触发（通过INT等指令），也可以是模块内部或者外部硬件触发的；后者主要根据针对中断是否要被处理（大部分中断都可以通过设置来配置成可屏蔽或不可屏蔽）。从实现上来分，有IDT(InterruptDescriptor Table)
和IVT(Interrupt Vector Table)之分。这部分的分类主要是根据x86模式来分的，IVT主要用于实模式，而IDT主要用于保护模式及之后的模式。IVT一般放在 系统 地址从0x0开始的4K空间，且一个Vector到实现代码之间的连接是比较直接的，Vector指针直接指向
代码段。而IDT表中的项还有一些转换才指向到代码，这也跟保护模式相适应。IVT或者IDT都可以通过IDTR寄存器来获取，而通过指令LIDT/SIDT可以读写IDTR寄存器。
1.x86 中断

目前为止 x86的中断控制器常用的为APIC（高级可编程中断控制器），而8259中断控制器随着Intel处理器的发展已经渐渐的被淘汰了，所以我们在这里也就不讨论了。

我们在说明中断控制器的时候，我们首先先来了解一下x86的中断处理过程，我们从CPU内部从里向外看，首先中断的处理的整体过程就是当发生了一个中断或者异常的时候，CPU去调用中断处理函数，而我们在进行中断初始化的时候，会提供中断向量表，正是由
