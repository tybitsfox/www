由于这种拜法我真没试过，所以我就不多说了
优点：随时随地能补血
缺点：纯靠龙输出

<font color=red>雷伊</font>——高速，其他都普通，倾向物理
拜师方案1.辅助，师匠—全程梅利普
雷伊大部分光辉都聚集在一开始，后期真的只能弄个辅助加开锁来混队伍了，用速度阵形也是他摆在最前面，不过如果能出ex回合的话，对整个战况都会有很高的帮助
优点：高速，有阵型对全体有很大帮助，还有加速技能
缺点：除了辅助没用处

拜师方案2.辅助以外，师匠—朵兰，龙神
辅助以外这个词很悲伤的，雷伊的最高武器是圣剑还是索命魔刀来着，圣剑吧，125攻击，雷伊到后期其实是有点薄的，而且血也不算多，用他输出兼辅助也就顶多了，
影子移动伤害也不高，唯一说得上的就是杀人虎，但是就算用了也没什么意义，他只能持续那点输出，真的不算多，而且看样子就算命令能一直持续，到后面也会解除
掉，很不安全
优点：可以输出，走向更广泛
缺点：不给力

