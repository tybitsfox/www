<font color=red>妮娜</font>——高智力，高速度，皮薄没攻击，典型法师
拜师方案1.输出，师匠—贤树，梅加斯，耶米泰
其实说实话妮娜真的没什么用，如果妮娜有回血技能还好，但是回血技能被那个主角抢走了，妮娜只剩下输出技能，但是就算是满智力的天雷，伤
害也在800-1200左右，不仅很不稳定，而且还不高，最重要的是没什么像影子移动那样的爆发技能啊，所以妮娜前期就是克制属性输出，后期替补+辅助
拜贤树加点防御，打过马兄弟去拜梅加斯，一路到长大后去拜梅加斯，最普遍的流程了吧
优点：前期克制高，有输出
缺点：后期替补没戏份

拜师方案2.辅助，师匠—迪兰达尔/贤树，梅利普
智力值差的不多的话，对技能影响真没多少，一回合就全补出来了，所以妮娜前期即便需要输出，也不需要加多少智力，拜迪兰达尔拿个技能就好了，或
者拜贤树加点防御，之后拜梅利普，妮娜没有自带回血，所以没有速度加成，只能扔药，所以高速还是很必要的
优点：高速奶，不挨揍，有ex回合扳胜负的关键
缺点：喜欢输出的冷板凳

