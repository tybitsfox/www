优点：速度快，不限制于攻击十分自由
缺点：冷板凳の诅咒

咱个人还是很喜欢加叔的，各种意义上，那种意义除外

<font color=red>小桃</font>——高攻高速高智力，纸片一样薄，命中还低
拜师方案1.强力输出流，师匠—巴巴德尔，龙神
