       奥拉克贝利<br>
       出了修道院往北，走不远就能到达奥拉克贝利镇。这里有一个豪华赌场，推荐有耐心的玩家在这里刷出50000代币来换一把金属王之剑，这样在以后的战斗中就会轻松许多。这里有一个怪物老爷爷，和他对话之后，得知主角拥有抓捕怪兽的能力。之后在夜晚可以在镇子西北角的房间内花300G买到马车，这样以后打倒怪物时，就有一定概率让怪物成为伙伴了。注意有些怪物能收，有些怪物是不能收的。打开怪物图鉴，可以收的怪物会有注明。<br>
       抓捕怪物是DQ5的一大特色，怪物有各种成长类型，有些初始的怪物，虽然能力比较低，但是成长度非常高，等级上限也比较高。推荐玩家在这里收一个红色的苹果怪，虽然等级上限只有20，但是初期的能力比较高。之后遇到史莱姆骑士的话一定要收一个，非常实用，而且具有抗火焰耐性。<br>
       ▼<br>
       圣塔罗斯洞窟<br>
       　 离开奥拉克贝利，往北走不久就能回到幼年时的圣塔罗斯村。进入这个村子之后发现村子已经一片荒凉，和村民对话之后得知是莱因哈特王下令放火烧了村子。在村里可以乘坐木筏进入幼年时代所无法进入的洞窟场景，在最深处可以找到一封帕帕斯留下的信和一把天空之剑。<br>
       离开村子，到达位于西边的阿尔卡帕，得知比安卡一家早已经搬走了。村里可以遇到一名来自莱因哈特的士兵，他会告诉主角莱 因哈特发生的政变，亨利的弟弟迪尔现在是莱因哈特国王，而实权却掌握在太后手中，施加重税，民不聊生。在旅馆里住一宿之后，亨利毅然决定回到自己的城堡里去一探究竟。<br>
       在圣塔罗斯洞窟里出没一种金属史莱姆，打倒之后可以获得1350点经验，一定要好好利用一下，给全员多多提升等级 。<br>
       ▼<br>
       莱因哈特洞窟<br>
       到达圣塔罗斯东边的关口，守卫的士兵认出是亨利王子，于是放主角通行。到达了莱因哈特城，城内的士兵却堵住去路不让主角一行入内。此时亨利王子想起来城内有一个秘密通路，可以乘坐竹筏绕到城内。竹筏就在城右侧幼年时代亨利王子被抓走的地方，秘密通道的入口就在城门的吊桥正下方。<br>
       进入城内的地下通路，绕到城内的庭院，在庭院里和小狗对话之后 会发生战斗。见到迪尔国王之后，国王认出了亨利，并把莱因哈特的钥匙交给了主角。使用这把钥匙可以进入亨利王子房间地下室的被锁的房门，里面有一个传送门，可以传送到森林深处的一个祠堂里。<br>
       得到莱因哈特的钥匙之后，建议先不要去传送门，回到城入口左边2层的宝物库，先把3个宝箱拿了，然后再出发也不迟。<br>
       ▼<br>
       神之塔<br>
       从祠堂出来之后，先不要朝南方走，先朝北边过桥，回到海边的修道院，在这里和一名修女对话之后得知如果想进入神之塔必须要有心灵纯洁的少女才能打开大门。这时，玛利亚自告奋勇表示原意与主角一行同去，带着玛利亚来到神之塔，就会发生剧情打开了神之塔的大门 。<br>
       进入塔内一层，居然看见了帕帕斯与主角母亲的幻影，由同伴对话得知神之塔内可以浮现出每个人的回忆。在神之塔移动的时候一定要小心，有一层必须要贴着路边走，万一失手掉下去就要重来。在塔最上面一层，有一处断桥，其实只有中间一格路是断的，左右两边都有看不见的路可以走。拿到拉之镜之后即可回城继续发展剧情，塔顶无BOSS。<br>
       回到莱因哈特城，来到王座上面一层，发现迪尔正因分不清真假太后而着急。对着太后使用拉之镜即可分辨出真假，之后进入BOSS战。<br>
       BOSS战：假太后<br>
