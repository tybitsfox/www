       波特塞米<br>
       打倒假太后之后，莱因哈特回复了和平。同时亨利和玛利亚也离开了队伍，临走前亨利送给主角一张双六卷。之后主角前往比斯达港，乘坐定期船到达西边大陆的波特塞米镇。<br>
       在这个村子的教堂左右两侧分别可以找到100G和一枚小徽章，此外，商店出售的魔法盾最好买一个，不但防御较高，还有隐藏的魔法抗性。<br>
       来到酒馆里会发生剧情，一名来自卡波奇村的村民来镇上寻找强力的战士讨伐怪物，结果却遭到强盗打劫。主角消灭了2只山贼狼之后，得救的村民委托主角一定要到他们的卡波奇村去帮忙消灭怪物，并预付了1500G报酬金。<br>
       旅馆内部有一个红色的房门暂时无法进入，需要以后得到魔法钥匙才能获得里面的宝箱。另外，这个村子也有怪物老爷爷，可以寄存怪物。<br>
       ▼<br>
       卡波奇村<br>
       离开波特塞米，朝南方前进到达卡波奇村。这个村子白天才会有人，如果是夜晚进入的话要在旅馆休息一下才能触发下一步的剧情。<br>
       白天进入村庄正中间的房屋，会见到之前的委托人和这个村子的村长。得知怪物就盘踞在村子西边的洞窟里，于是主角出发朝洞窟前去一探究竟。<br>
       ▼<br>
       魔物巢穴<br>
       洞窟比较难走，需注意别掉落陷阱了。在洞窟深处，可以发现一只杀人豹，它身后还有一把剑。在和杀人豹战斗之前，一定要把比安卡的缎带携带在 主角身上，战斗中只要使用比安卡的缎带，杀人豹就会认出主角，并加入同伴。之后能得到帕帕斯留下的剑，只有主角可以装备。回卡布奇村和村民对话，可以得到另一半1500G谢礼。<br>
       山洞里有一个骷髅旁边可以找到一枚小徽章，别忘了拿了。杀人豹加入时身上会有新的装备品。<br>
       ▼<br>
       鲁拉菲恩<br>
       从卡波奇出发，回到波特塞米再一路向西，即可到达鲁拉菲恩镇。这个镇子内部构造复杂，需旋转视角才能找到正确的路线。<br>
       在镇子内能找到一个研究魔法的老人，和他对话之后得知他在试图让古代消失的种种魔法复活，最近在实验一种传送魔法，不过需要一种名为鲁拉月草的道具，老人在地图上指出了鲁拉月草的具体位置，让主角去寻找。<br>
       鲁拉月草的位置在鲁拉菲恩所在的大陆西南角，不过只有夜里才能找到，这里就要灵活使用于圣塔罗斯洞窟里找到的黑暗之灯了。找到鲁拉月草之后再回去和老人对话，即可习得传送魔法鲁拉，之后亲自使用一次证明实验成功即可。<br>
       DQ5在历代作品中比较特殊，如果想随意传送到任意都市，只能使用鲁拉咒文。而道具店里出售的奇美拉翅膀只能飞到最后一次离开的城镇，不能自由选择移动对象。<br>
       ▼<br>
       萨拉波纳<br>
       在鲁拉菲恩南方野外有一个旅馆，旅馆再往南有一个通往萨拉波纳的洞窟。不过在这之前必须要得到鲁拉咒文，并且传送回莱因哈特城恭贺一下亨利和玛利亚的新婚，否则洞窟将会被守卫封锁无法前进。<br>
       来到萨拉波纳镇后，在入口会与鲁德曼的女儿——芙罗拉相会。接着前往镇子西北方的鲁德曼豪宅，在这里和女佣及门口等待的村民对话之后即可发生剧情。鲁德曼许下诺言，如果有人能够得到火之戒指和水之戒指，就把自己的女儿许配给对方。<br>
       为了得到鲁德曼的家宝——天空之盾与船只，主角只得前往死火山去寻找火焰戒指……<br>
       在鲁德曼的豪宅3楼（虽然外表看起来仍然是二楼……）是DS版新追加的角色——黛博拉的房间。房间内有一个使用触摸屏的史莱姆迷你游戏机，可以使用触摸屏进行类似于打地鼠的小游戏，如果得分较高还能得到奖励，推荐有兴趣的玩家试试。<br>
       ▼<br>
       死火山<br>
       进入洞窟不久后可以见到为了迎娶鲁德曼的女儿芙罗拉而来冒险的安迪。洞窟二层以后开始出现了伤害地形，每走一步都要损伤一点HP。在第三层有一处回复点，可以灵活运用。在最深处调查墙壁上的火之戒指之后会出现3只熔岩原始人，之后进行BOSS战 。<br>
<br>
BOSS战：熔岩原始人<br>
       HP:420　攻击力：90　守备力：60　速度：21 经验：350 金钱：250 留下物品：魔法圣水<br>
       行动规律：火焰之吐息、普通攻击、呆望随机行动一种。<br>
       咒文抵抗：玛努沙和 鲁卡尼高概率有效、拉里荷和美达巴尼低概率有效，梅干地无效。<br>
       攻略要点：这一战是DQ5最难的一战，BOSS3只同时出现，而且喷火伤害大概有40～60点，所以只有祈祷3只千万别同时喷火。另一方面，开战后一定要全力攻击，争取在最短回合内击倒一只，这样一来就能轻松许多。<br>
       ▼<br>
       深山之村<br>
       得到火之戒指之后，回到萨拉波纳和鲁德曼对话，即可得到一艘小船，可以用来在海面上航行前往水之戒指所在的瀑布洞窟。<br>
       离开萨拉波纳不久，北方会有一个水门挡住去路，需要到旁边的深山之村打开水门才行。村内的杂货店内一个坛子打碎就会发生战斗，该怪物实力较为强劲，最好回避。在深山之村里可以和幼年时的玩伴——比安卡再会。和她诉说原委，比 安卡成为伙伴加入队伍，并为主角打开了水门。<br>
       ▼<br>
       瀑布洞窟<br>
       穿过水门到达北边的瀑布洞窟，这个洞窟没有什么难点，深处也没有BOSS，注意要节省MP使用咒文回复即可。在最深处得到水之戒指之后回村，然后去 鲁德曼的豪宅内即可触发剧情。<br>
       接下来就是万众瞩目的DQ5结婚剧情了，鲁德曼给 主角一晚上的时间来仔细考虑。次日早上来到鲁德曼的豪宅即可挑选自己喜爱的新娘（DS版新加了黛博拉乱入的剧情）。选任何一个新娘都不会影响到后盘的剧情发展，只不过3个新娘的能力上有些细微的区别。挑选新娘之后，去深山之村的杂货店拿面纱，再回村即可举办婚礼。<br>
       ▼<br>
       迪尔巴多<br>
       如果玩家选择的结婚对象是芙罗拉或者是黛博拉的话，第二天鲁德曼会 拜托主角去村北的封印祠堂看看。在祠堂最深处调查一个巨大的壶，壶的颜色是蓝色的。回去把这个消息告诉鲁德曼，鲁德曼就会让主角使用自己停驻于波特塞米的联络船。如果选择和比安卡结婚的话，这一段剧情将会直接跳过，鲁德曼会直接把波特塞米的联络船送给主角。<br>
       有了船只可以在海洋上尽情的冒险，这里主要可以到南边沙漠的迪尔巴多城和徽章王之城、沙漠北边小岛上的名产品博物馆去 看看，不过这些都不是下一步的主线剧情。迪尔巴多城会见女王之后，得知女王手中拥有天空之盔，不过目 前还没有找到传说中的勇者，看来只有以后才能来拿了。<br>
       感兴趣的玩家也可以到位于地图正中央的赌场之船去看看，这里的代币和奥拉克贝利赌场是通用的。<br>
       ▼<br>
       尼多之旅店<br>
       由沙漠城市迪尔巴多往东横穿海洋，可以到达东边大陆上的尼多之旅店，在这里建议休整一下，并保存记录。之后要去的位于北边的通往古兰巴尼亚的洞窟是DQ史上最漫长的迷宫，一定要做好心理准备。<br>
       ▼ 奇索特山道<br>
       在奇索特山道上有一个地下室，在这里和一个老婆婆对话之后可以免费住宿，之后夜里主角被磨刀的声音惊醒，后来才得知原来是老婆婆在给主角打造装备，于是主角提升了5点攻击力。如果玩家选择与芙罗拉或者是黛博拉结婚的话，在洞窟门前会有一名士兵，他会带来鲁德曼赠送的宝箱。接着会有一个旅行的神父，如果主角身亡的话，神父能够免费给主角复活。<br>
       ▼<br>
       奇索特村<br>
       刚进奇索特村，会发生剧情，主角的新娘会突然晕倒。之后经过诊察，原来是长途跋涉所致，并无大碍。在这里可以稍微休整一下，并可以记录存档。之后还有通往古兰巴尼亚的山洞另一半，大家要做好心理准备。<br>
       ▼<br>
       古兰巴尼亚山的洞窟<br>
       在山洞里路线十分复 杂，正确的路线必须从某个陷阱跳下去才能到达，玩家必须自己慢慢摸索了。洞窟里的怪物非常棘手，特别是一个手持法杖的僵尸可以召唤闪电全体攻击，对我方伤害巨大，推荐玩家需多留意MP剩余量，实在不行，先传送回奇索特村休息一下再出发也行。这里有不少宝箱都是怪物，打倒的话有很高几率留下小徽章，不过，宝箱怪的全体即死咒文成功率非常高，这一点非常头疼。<br>
       在山洞的某个出口里 ，会遇到一个神秘的老人，他会询问你是否丢失了一个双六卷，此时一定要选择否；之后他会继续询问是否丢失了一个小徽章，继续选否；接着他又会问你是否丢失了水之羽衣，再选否的话，他会说你是个诚实的人，于是把3样物品都送给了主角。<br>
       ▼<br>
