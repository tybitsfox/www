<p>从無敵フィットネス再继续往南走一点就可以看到一个很小写着铁字的据点.这个据点アイアンクラウン(铁皇冠).这个小镇里面可以进行战车的最后大改造!不过先别高兴的太早.因为进入镇子后,只要与NPC对话2次赏金BOSS就会出现,抓走所有的人.所以如果此时战车状态不佳就先补弹药装甲吧.赏金首抓走所有重要NPC后,先要离开小镇一下,再进入与老头对话.老头就会委托日向从赏金首的手上夺回镇民.获得任务后只要出门赏金首就会被标在地图上(红色圆点).就在镇子的西南方(注意这个赏金首会拷贝我方一个单位的战斗力所以不是那么好对付)击败后,赏金首吐出所有NPC逃到南方的结晶洞.先不管赏金首.</p>
<p>回到镇子里大改造吧.镇子里可以将战车的第六洞打开,但是战车就会变成单引擎或单C所以要慎重考虑.而还有一个新的NPC可以扩展C装置的能力(各C装置都有不同的极限)个人推荐红狼战车C装置用連動ヤヌス扩展3此就有.主炮2连,会心一击,ダブルストライク.然后地盘特性改成迎击能力.这样第一主炮装备宇宙炮也能有很不错的战斗力.而且也有了迎击能力.</p>
<p><img  width='400' height='240' src='./wp-content/uploads/2016/01/HNI_0113.jpg' ></p>
<p>另外镇子附近的母凹凸曼没什么好东西可以掉的,就一门很0,07T的副炮.所以直接灭掉吧</p>
<p><img  width='400' height='240' src='./wp-content/uploads/2016/01/HNI_0114.jpg' ></p>
<p>改完战车后,如果想收拾掉赏金BOSS,就先前往南面的结晶洞吧.洞内用金属探测器能探索到很多铁渣和改造战车的珍贵金属.只要和洞内的人一路对话最终就可以将这变形怪追上绝路,但是这怪会分裂还须要一次人类战.而且分裂的每个都会复制我方的战斗力.所以要与其战斗推荐先回到-悪党ミュージアム往东北一点有个镇子-ウンガウンガ(运河运河)镇,在里面可以补给人类武器,里面有卖スーパー殺虫ガン.用这超级杀虫枪可以比较容易干掉BOSS(最好战斗前把这把武器以外的武器都卸掉,这样BOSS就只能复制这个武器).当</p>
<p><img  width='400' height='240' src='./wp-content/uploads/2016/01/HNI_0115.jpg' ></p>
