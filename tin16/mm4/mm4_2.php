<p>一出达姆就来到雪地.沿着左边边缘一直往南走可以看到一个据点-悪党ミュージアム,这里能租车(红狼剩下的一台也在这租而且带迎击神话但是要先游戏通关一遍).一辆5主炮车被NPC给租走暂时没法得到.</p>
<p><img  width='400' height='240' src='./wp-content/uploads/2016/01/HNI_0107.jpg' ></p>
<p>要这两破车的话可以到悪党ミュージアム东北一些的雪地上,会看见那个NPC和33W的赏金首機神機関对战.战后这车就领便当回去了.不过機神機関不是很强,用猎人+电光石火射宇宙炮改一会就能消灭.消灭后可以得到发动机機神機関(和怪名字一样)有90多T的载重,武器的大炮3星改满2000多攻击力99发子弹..但是重量自然也..~最后这个BOSS还会掉一个C装置3会心.这个一定要刷.给宇宙炮装上基本除了会心就只有会心了.</p>
