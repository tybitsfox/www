<p><a name='準備事項'></a></p>
<p><span style='color: #0000ff;'>準備事項</span></p>
<p>取得飛空艇獵隼號之後，現階段已經可以進入正常主線劇情的最終迷宮瓦礫之塔，不過我們當然不會就這麼讓 4 個角色進去，還有很多的伙伴沒有加入，讓我們一一帶他們歸隊。另外還有幾件事情可以先交待一下。</p>
<p><a name='同伴回收計劃'></a></p>
<p><span style='color: #0000ff;'>同伴回收計劃</span></p>
<p>在一般攻略之下，同伴的回收順序沒什麼太大的差別，但是在低等級攻略下，會因為隊伍的平均等級所影響，所以必須安排好順序。目前 4 名角色都是 Lv 6，而後續比較重要的洛克、莫古，一個在鳳凰洞窟，一個在納爾謝，且莫古等級 Lv 11 較高，而找莫古的時候可以得到低等級攻略中相當重要的飾品 ( 效果是不會遇敵 )，對於鳳凰洞窟及日後的攻略上起絕對性的作用。</p>
<p>洛克重要的原因除了可以偷盜之外，在納爾謝部份上鎖的屋子必須有他在場才能打開，這關係著一些重要的裝備、魔石的取得，所以洛克也會是一般攻略中加入順序排得很前面的角色。</p>
<p>不過先找莫古的話，隊伍平都等級會變 ( 6&#215;4 + 11 = 35 ) / 5 = 7，這樣再去找洛克的時候，洛克的等級會變成 Lv 7，所以在莫古與洛克之前，需要再加入一名 Lv 6 的同伴，上一篇我們有去找過一次蒂娜並且有一場打了一半的 BOSS 戰，所以下一個要加入的人選就是蒂娜，接著就是莫古、洛克，然後才是其他隊員。蒂娜加入後再去找莫古，平均等級為 ( 6&#215;5 + 11 = 41 ) / 6 = 6.8，這樣後續去找洛加入的時候就會是 Lv 6。</p>
<p>嘎嗚的等級 Lv 16 最高，但因為獸原可以放生的特性，能夠隨時以三人隊伍前往獸原找嘎嗚，然後去學新增的狂怒技，或是收集怪物圖鑑等等，利用完之後再放生即可。不過現階段還不用急著去獸原找他，先照著上面的蒂娜、莫古、洛克的順序來進行即可。</p>
<p><a name='魔石：綠咬鵑'></a></p>
<p><span style='color: #0000ff;'>魔石：綠咬鵑</span></p>
<p>乘飛空艇來到崩壞後的第一個地點：孤島 ( 位置在世界地圖的正南方偏左側 )</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-2/01-prepare-001.png' alt='' width='486' height='326' /></p>
<p>前往之前塞麗絲補魚的海邊，可以找到一個魔石：<span style='color: #800000;'>綠咬鵑</span></p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-2/01-prepare-002.png' alt='' width='486' height='326' /></p>
<p><a name='必殺技：夢幻鬥舞'></a></p>
<p><span style='color: #0000ff;'>必殺技：夢幻鬥舞</span><br /> 在世界地圖的正北方左右 ( 請參考下圖位置，在納爾謝的東北方 )，有五顆樹排成十字形，朝著正中間那顆樹可以進入一個小屋</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-2/01-prepare-003.png' alt='' width='486' height='326' /></p>
<p>一進來就是崩壞前科爾茲山的背景音樂，要進屋前馬修的師父「鄧肯」出現</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-2/01-prepare-004.png' alt='' width='486' height='326' /></p>
<p>經過師父的實戰教學後，馬修學會了最強必殺技：夢幻鬥舞！</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-2/01-prepare-005.png' alt='' width='486' height='326' /></p>
<p>在選單中的「特殊、必殺技」中查看，就會知道夢幻鬥舞的輸入方式，從左開始，往上順時鐘 360 度轉一圈回到左。( 一個一個按的話，斜向，例如左上，可以用左或上來代替 )</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-2/01-prepare-006.png' alt='' width='486' height='326' /></p>
<p><a name='特殊敵人：仙人掌'></a></p>
<p><span style='color: #0000ff;'>特殊敵人：仙人掌</span></p>
<p>在馬蘭達鎮南方的一小塊沙漠區 ( 如下圖 )，會出現特殊敵人 <span style='text-decoration: underline;'><span style='color: #000080;'><a href='./ff6_images/ff6_monsters/a073.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A073-仙人掌</span></a></span></span></p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-2/01-prepare-007.png' alt='' width='486' height='326' /></p>
<p>攻略方式：</p>
<p>仙人掌的 HP 只有 3 點，但魔防、物防、閃避率都是極高，一般攻擊無法傷他，而且有時候會逃走，而現階段最簡單的攻擊方式是讓角色裝備「<span style='color: #800000;'>究極武器</span>」與「<span style='color: #800000;'>狙擊之眼</span>」( 物理攻擊必中 )、「<span style='color: #800000;'>赫耳墨斯之靴
</span>」及其他可增加速度的裝備，一刀砍下去就能秒殺，當然「<span style='color: #800000;'>疾風髮簪</span>」( 提高先發制敵機率 )、「警鈴耳釘」( 防止背後、兩面受敵 ) 這兩個飾品也可以都找人裝備著。</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-2/01-prepare-008.png' alt='' width='486' height='326' /></p>
<p><span style='text-decoration: underline;'><span style='color: #000080;'><a href='./ff6_images/ff6_monsters/a073.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A073-仙人掌</span></a></span></span> 的經驗值為 0，但是魔法修煉值有 10 點，
戰後金錢有 10000 元。在低等級攻略中，可以說是世界崩壞後賺錢、學魔法的最佳地點，SQUARE ENIX 一定是刻意設計這個敵人的XD，仙人掌除了究極武器可以砍之外，後期取得命中率 100% 的傳說神劍也可以拿來砍仙人掌。</p>
<p>另外要提一下的是這個小沙漠裡面還有另一個敵人 <span style='text-decoration: underline;'><span style='color: #000080;'><a href='./ff6_images/ff6_monsters/a248.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A248-礦地爬蟲</span></a></span></span>，HP 12018，他
會使用沙塵暴的全體攻擊，傷害都是上千的，低等級攻略遇到逃跑即可。一般正常攻略時，在現階段還沒取得強力裝備之下，可以試試對他用 <span style='color: #800080;'>即死</span> 魔法，或是 <span style='color: #800080;'>停止</span> 魔法，再丟 <span style='color: #800000;'>玄冰棒</span>來攻擊，若怕中途出
差錯被沙塵暴全滅，可以找個人裝備 <span style='color: #800000;'>龍騎士之靴</span> 在最後跳上去躲過，或是等日後有重生魔法，或嘎嗚入隊後再來解決他就好。</p>
<p><span style='text-decoration: underline;'><span style='color: #000080;'><a href='./ff6_images/ff6_monsters/a248.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A248-礦地爬蟲</span></a></span></span> 也有 5 點的魔法修煉值與 10000 元的收獲，但是他還有 7524 的經驗值，如果你是全程低等級攻略，那一樣去獸原找他就好了，沒有要升級就別在地圖上打。</p>
