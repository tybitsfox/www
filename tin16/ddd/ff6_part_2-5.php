<p>這次有兩個方式提供參考，下面會有說明：</p>
<p>(A) 方法 &#8211; 指令：<strong>盜取</strong> x3／飾品：<span style='color: #800000;'>盜賊手鐲 + 赫耳墨斯之靴</span></p>
<p>(B) 方法 &#8211; 指令：<strong>盜取</strong> x3／飾品：<span style='color: #800000;'>盜賊護手</span> ( 盜取會自動變奪取 ) + 祕傳之證</p>
