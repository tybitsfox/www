<p>莫古裝備 <span style='color: #800000;'>雪絨圍巾</span>，搭配其他裝備讓物防達 255，加上飾品<span style='color: #800000;'> 騎士心得</span>，其餘隊員保持瀕死狀態，可以完全防禦一般的物理攻擊。( 要保持瀕死狀態的角色請勿裝備奇跡之靴或其他會自動回復的飾品 )</p>
<p><strong><span style='color: #800000;'>B 戰術</span>：弱點製造器 + 投擲神聖棒</strong></p>
<p>使用埃德加的機械 弱點製造器 不斷追加敵人弱點，尤其是聖屬性，再以神聖棒投擲攻擊。</p>
<p><strong><span style='color: #800000;'>C 戰術</span>：塞策 —&gt; 千王骰 + 祕傳之證</strong></p>
<p>低等級中無屬性攻擊的強者之一，基本上就是賭博來的&#8230;.靠三顆骰子的點數變化來產生傷害，一樣無視防禦無視回避，搭配祕傳之證可以連丟 4 次骰子，有時候這招也是相當可靠的！</p>
<p><strong><span style='color: #800000;'>D 戰術</span>：步數傷害 + 三星項環 + 神速</strong></p>
<p>低等級攻略到了後期，這個方式會是主要的進攻手段，因為步數傷害、神速的消耗 MP 太高，只能利用三星項環這個飾品才能連續使用。可以使用步數傷害的人有三個：嘎嗚、斯特拉格斯、格格。</p>
<p>學識 ( 青魔法 )：步數傷害 可以直接選擇使用</p>
<p>狂怒技 &#8211; 步數傷害：<span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a047.png' target='_blank'>A047-法夫納</a></span></span>、<span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a074.png' target='_blank'>A074-巨型爬蟲</a></span></span>、<span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a081.png' target='_blank'>A081-洋蔥衝鋒兵</a></span></span></p>
