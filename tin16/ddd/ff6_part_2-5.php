<p>整個遊戲中要取得 <span style='color: #800000;'>三星項環</span> 這個逆天又無恥的飾品共有四種方法：</p>
<p>(1) 到恐龍森林打 <strong>雷龍</strong>，低機率掉落 ( 因為有經驗值，而且極高，故低等級之下需在獸原才行 )</p>
<p>(2) <span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a246.png' target='_blank'>A246-伽琉普德斯鳥</a></span></span> 身上低機率偷盜 ( 鳳凰洞窟內入口處、獸原 )</p>
<p>(3) 競技場：以 <strong>薩瑪莎靈珠</strong> 交換，敵人為 <span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/b060.png' target='_blank'>B60-凶惡巨獸王</a></span></span>，打敗後獲得</p>
<p>(4) 競技場：以 <strong>天叢雲劍</strong> 交換，敵人為 <span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a246.png' target='_blank'>A246-伽琉普德斯鳥</a></span></span>，戰鬥中低機率偷盜</p>
<p>上面四種方式，(1) 如果你等級頂天，那就去打雷龍慢慢等他掉，不過機率太低很浪費時間。(2) 的出現機率太低，獸原也是一樣。(3) <strong>薩瑪莎靈珠</strong> 整個遊戲只有一個，而且是唯一可以使出連續魔法的珍貴飾品，說什麼也不能拿來換吧。所以呢，就剩下 (4) 了，固定出現，只要能夠不限次數取得 天叢雲劍 就可以了 ( 下面會說明 )。</p>
<p>交換流程：</p>
<p>第一階段：神聖槍 → 天叢雲劍</p>
<p>第二階段：天叢雲劍 → 神聖槍</p>
<p>如果你原本就有 天叢雲劍 也可以先跳過第一段，之後有多的神聖槍再繼續來換。用天叢雲劍去賭的話，出場的就是「<span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a246.png' target='_blank'>A246-伽琉普德斯鳥</a></span></span>」，目的就是偷取「三星項環」而已，勝負沒有影響。只是因為偷盜「三星項環」是低機率，一般都是偷到 鳳凰尾巴，如果沒偷到就直接退出遊戲，重新繼續。</p>
<p>神聖槍 第一次取得是在狂信者之塔打敗 <span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/b072.png' target='_blank'>B072-聖龍</a></span></span> 後取得，之後在獸原戰鬥時，每一輪循環都會出現聖龍，在它身上可以低機率偷到，雖然每一輪打到聖龍要花不少時間，但是反正 英雄盾 也是要花時間才能升級好的，又要收集狂怒技、怪物圖鑑什麼的，有遇到聖龍就記得一定要偷神聖槍，量產一定的數量後 ( 一般 4 把就夠了，要收集道具的話自己多弄一些 )，就可以來偷 三星項環。</p>
<p><strong>第一階段：神聖槍 → 天叢雲劍</strong></p>
<p>敵人：<span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a223.png' target='_blank'>A223-死亡機器</a></span></span></p>
