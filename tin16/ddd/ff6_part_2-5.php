<p><a name='巨龍首競技場'></a></p>
<p><span style='color: #0000ff;'>巨龍首競技場</span></p>
<p>目前最終戰前的地點只剩下一個瓦礫之塔，不過在那之前，我們可以到巨龍首競技場去換一些值得入手的道具及武器防具，甚至還有在競技場才能收到的怪物圖鑒。而怪物圖鑒及狂怒技的收集，因為已經沒有什麼過了就收集不到的地點，所以可以等到最後閒閒沒事(?)再去收集即可。</p>
<p>巨龍首競技場在地圖的最左上方位置</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-5/01-colosseum_001.png' alt='' width='486' height='326' /></p>
<p>競技場的敵人有強有弱，如果你等級很高，當然打起來不難，隨便裝備一下放牛吃草也能一一解決，而低等級的挑戰之下就會顯得吃力一些，畢竟戰鬥過程中是無法控制角色的，所以在低等級的競技場攻略上自古以來就衍生出許多針對性的打法。</p>
<p>值得一提的是，競技場中出現的敵人，如果你有打敗，也會計入怪物圖鑑的收集。例如下面出現的不少種怪物都是瓦礫之塔才有出現的，這裡有打死的話也會直接收入圖鑑。</p>
<p>然而競技場的戰鬥並不是絕對要去挑戰的，但既然 FF6 都放了這麼有趣的元素在遊戲中，不換不是很可惜嗎？以下只是列出一般都會推薦要換的，有其他心得也歡迎大家分享。以下打法皆以低等級攻略為主，一般攻略也可參考，防具上當然有 <span style='color: #800000;'>英雄盾</span> 會更好，但這個就不多提了。</p>
<p><a name='貓耳兜帽、勳章'></a></p>
<p><span style='color: #0000ff;'>貓耳兜帽、勳章</span></p>
<p>道具：<span style='color: #800000;'>沙悟淨之槍</span> → <span style='color: #800000;'>貓耳兜帽</span> ( 數量不拘，但至少留一個給瑞姆裝備用 )</p>
<p>( 貓耳兜帽：瑞姆專用防具，使用後戰鬥結束金錢加倍，打仙人掌很好賺錢 )</p>
<p>敵人：<span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a199.png' target='_blank'>A199-變異龍</a></span></span>／HP 3000</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-5/01-colosseum_002.png' alt='' width='486' height='326' /></p>
<p><strong>攻略角色：塞策</strong></p>
<p>1. 裝備：千王骰 ( 點數攻擊 )，其他防具就挑數值高的就好</p>
<p>( 備註：千王骰 是之前用莫古的帶著莫魯魯護身符去瓦礫之塔取得的 )</p>
<p>2. 飾品：祕傳之證 ( 四次攻擊 )、祈安珠 ( 防止致死一擊、石化、殭屍 )</p>
<p>3. 基本上這樣裝備之下不太會失敗了，貓耳兜帽下面可以繼續換</p>
<p>道具：<span style='color: #800000;'>貓耳兜帽</span> → <span style='color: #800000;'>勳章</span></p>
<p>敵人：<span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a248.png' target='_blank'>A248-礦地爬蟲</a></span></span>／HP 12018</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-5/01-colosseum_003.png' alt='' width='486' height='326' /></p>
<p><strong>攻略角色：洛克、暗影</strong> ( 建議魔法越少越好，完全不要學最好 )</p>
<p>1. 裝備：一擊之刃 or 暗殺短劍</p>
<p>2. 飾品：赫耳墨斯之靴、天使羽毛 ( 防地震攻擊 )</p>
<p>3. 只要角色使出普通攻擊，就有機會追加即死獲勝，因對手攻擊較強，務必先存檔預防萬一</p>
<p><a name='有機劍'></a></p>
<p><span style='color: #0000ff;'>有機劍</span></p>
<p>道具：<span style='color: #800000;'>火舌</span> → <span style='color: #800000;'>有機劍</span></p>
<p>敵人：<span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a262.png' target='_blank'>A262-大魔界花</a></span></span>／HP 7000</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-5/01-colosseum_004.png' alt='' width='486' height='326' /></p>
<p><strong>攻略角色：格格</strong></p>
<p>1. 把埃德加的機械，除了「<span style='color: #800000;'>空氣錨、回轉電鋸</span>」這兩個之外都賣掉 ( 只有這兩個買不到 )</p>
<p>2. 開戰前記得先去存檔，最低等級的狀態下有失敗機會，高等級就隨便了XD</p>
<p>3. 去 <strong>選單 &gt; 狀態</strong> 將格格的戰鬥指令三格都調成「機械」</p>
<p>4. 飾品：赫耳墨斯之靴、緞帶</p>
<p>5. 格格使出空氣錨的機會很高，只要對方下次行動沒有讓你躺下，就成功了</p>
<p><a name='雪絨圍巾'></a></p>
<p><span style='color: #0000ff;'>雪絨圍巾</span></p>
<p>道具：<span style='color: #800000;'>凶惡巨獸套裝</span> → <span style='color: #800000;'>雪絨圍巾</span></p>
<p>敵人：<span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a219.png' target='_blank'>A219-化外忍者</a></span></span>／HP 8050</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-5/01-colosseum_005.png' alt='' width='486' height='326' /></p>
<p><span style='color: #0000ff;'>說明 1</span>：<span style='color: #800000;'><strong>雪絨圍巾</strong></span> 冰吸收、火半減，而且還是物防最高的防具 ( 128 )，搭配其他防具會更強大，物防輕鬆就能組合到 255，敵人攻擊馬上降到 0 或 1。可惜這件防具只有 <strong>莫古</strong> 與 
<strong>嘎嗚</strong> 可以裝備 ( 烏瑪羅本身有內建 )，所以取得 2 件其實就夠了。莫古穿上這件，再加上飾品 <span style='color: #800000;'>騎士心得
</span>，基本上已可確保物理攻擊相安無事。至於<span style='color: #800000;'> 凶惡巨獸套裝</span> 之前打 <span style='text-decoration: underline;'><span style='color: #000080; text-decoration: underline;'>
<a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/b060.png' target='_blank'>B060-凶惡巨獸王</a></span></span> 的時候應該有取得 2 件，剛好用來換。如果要收集凶惡巨獸套裝的就自行再去獸原打，每一輪都會出現凶惡巨獸王。</p>
<p><span style='color: #0000ff;'>說明 2</span>：在低等級之下，ABC 三個角色我都有試過能成功，真要說贏面大一點的話我推洛克，因為速度最快，而敵人的速度也很快，所以當然就以快制快。不過因為敵人常出 異次元、核爆、手裡劍 這種讓低等級會瞬間掛點的招式，主要重點還是要提高先發制敵的機會，所以疾風髮簪必裝。</p>
<p><strong>攻略角色：洛克、暗影、塞策</strong></p>
<p>( 三選一，基本選擇會魔法最少的，然後&#8230;塞策成功機率較低XD )</p>
<p>必要飾品：源氏之護手 ( 可裝備兩把武器 )、疾風髮簪 ( 提高先發制敵機率 )</p>
<p>(A) <strong>洛克</strong> &#8211; 飾品：源氏之護手／武器：<span style='color: #800000;'>暗殺短劍 + 翼刃</span> ( 都有一擊必殺效果 )</p>
<p>(B) <strong>暗影</strong> &#8211; 飾品：源氏之護手／武器：<span style='color: #800000;'>暗殺短劍 + 一擊之刃</span> ( 都有一擊必殺效果 )</p>
<p>(C) <strong>塞策</strong> &#8211; 飾品：源氏之護手／武器：<span style='color: #800000;'>一擊之鏢 + 死神塔羅牌</span> ( 都有一擊必殺效果 )</p>
<p><a name='改名卡、奇跡之靴'></a></p>
<p><span style='color: #0000ff;'>改名卡、奇跡之靴</span></p>
<p>道具：<span style='color: #800000;'>萬靈藥</span> → <span style='color: #800000;'>改名卡</span></p>
<p>敵人：<span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a073.png' target='_blank'>A073-仙人掌</a></span></span>／HP 3</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-5/01-colosseum_006.png' alt='' width='486' height='326' /></p>
<p><strong>攻略角色：</strong>只要可以裝備「<span style='color: #800000;'>究極武器</span>」的皆可</p>
<p>( 原則上也是魔法越少的人越好，比較不會亂出招 )</p>
<p>1. 裝備：究極武器，防具隨意</p>
<p>2. 飾品：<span style='color: #800000;'>赫耳墨斯之靴</span>、<span style='color: #800000;'>狙擊之眼</span> ( 物理攻擊 100% 命中 )</p>
<p>3. 記得先存檔。仙人掌若一開始就放千針刺就重來吧，只要你的角色出普通攻擊就必勝！</p>
<p>4. 改名卡可以繼續換很棒的夢幻飾品 <span style='color: #800000;'>奇跡之靴</span>，所以要換多少就看你自己需求</p>
<p>道具：<span style='color: #800000;'>改名卡</span> → <span style='color: #800000;'>奇跡之靴</span></p>
<p>敵人：<span style='text-decoration: underline;'><span style='color: #000080;'><a style='color: #000080; text-decoration: underline;' href='./ff6_images/ff6_monsters/a258.png' target='_blank'>A258-魔神龍</a></span></span>／HP 18008</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-5/01-colosseum_007.png' alt='' width='486' height='326' /></p>
<p><strong>攻略角色：格格</strong></p>
<p>1. 裝備：短劍隨意／防具：<span style='color: #800000;'>寒冰盾</span>／飾品：<span style='color: #800000;'>赫耳墨斯之靴</span></p>
<p>2. 指令：速寫 x2、攻擊 x1</p>
<p>3. 由於最低等級狀態，所以失敗機會挺高，目的是要使用「速寫」，成功的話有三分之一的機會可以使出魔神龍的「無心天使」，讓對方 HP 馬上降為 1，再等待一次普通攻擊就能擊敗，過程可能會比較長，需要一點耐心。</p>
<p>4. 奇跡之靴自帶「魔防、物防、加速、再生」四項功能，非常好用，一定要多換一些！</p>
