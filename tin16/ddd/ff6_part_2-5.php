<p>4. 沒偷到 三星項環 就離開遊戲重新繼續吧，一旦偷到就等到戰鬥結束即可</p>
<p>5. 經驗參考：我寫攻略時偷了 4 個三星項環，A、B 方法各成功兩次，場次算起來沒差多少，也就是說成功機率蠻接近的。這樣一來到底哪一個比較好是不一定的，自己用喜歡的方式吧！</p>
<p>6. 一般攻略之下，三星項環 也是這樣偷會比較輕鬆，打雷龍一整個煩死了XDDD</p>
