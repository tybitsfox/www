<p>天花板上有很多隨機移動的老鼠敵人，正常攻略下一一清空，時間內到達 BOSS 處就會進入 BOSS 戰，但最低等級攻略請參考下面說明</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_1-4/02-opera-017.png' alt='' width='486' height='326' /></p>
<p><span style='color: #008000;'><strong>★低等級攻略建議：</strong></span></p>
<p>1. 最低等級攻略的大難關，歌劇院老鼠回避 ( 無法逃跑 )，在手機版上操控更加困難之下，若想要全部回避可以說是相當困難，也許需要重新讀擋數十次才能成功 ( 不過話說&#8230;.我大約 15 次後就成功了&#8230;電腦比不過人腦XD )</p>
<p>2. 老鼠一共有兩種 ( 綠色、黃色 )</p>
<p><span style='color: #ff0000;'>★關於老鼠的特別提醒：( 低等級攻略限定 )</span></p>
<p><span style='color: #ff0000;'>要收集狂怒技與怪物圖鑑的人，請注意第一場務必要自行全滅一次 ( 兩種老鼠都會有 )，被踢出歌劇院之後直接開選單去「存檔」</span>，這個時候該敵人便會在獸原登錄，日後狂怒的學習與圖鑑的收集便可做到。第二次讀取存檔進入後，再正式開始閃老鼠的任務。當然，你甚至可以在被踢出去的時候，千里沼沼地先回去獸原打老鼠，不過我是沒那麼多時間&#8230;&#8230;</p>
<p>3. 天花板的地圖參考下圖 </p>
<p><img class='alignnone' src='./ff6_images/ff6_part_1-4/02-opera-018-1.png' alt='' width='298' height='268' /></p>
<p>兩種老鼠分別是：</p>
<p><span style='text-decoration: underline;'><span style='color: #000080;'><a href='./ff6_images/ff6_monsters/a032.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A032-強襲鼠</span></a></span></span> ( 綠色 )、<span style='text-decoration: underline;'><span style='color: #000080;'><a href='./ff6_images/ff6_monsters/a033.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A033-妖咒鼠</span></a></span></span> ( 黃色 )</p>
<p>分享一下我自己回避老鼠的技巧，實際上因為老鼠的移動是隨機的，沒有一定的最佳路線，每個人遇到的難關恐怕都不一樣，甚至我也用了兩支手機交叉測試路線，最佳的路線也都不同。</p>
<p>當限時 5 分鐘開始後，用最快的速度去打開右房間的開關，接著馬上前往左房間的天花板，一進入到老鼠區域，首先找一組老鼠遇敵 ( 正常來說第一組都是三隻，黃色綠色都會有 )，在戰鬥時自行全滅結束戰鬥，回到歌劇院外面後，調整角色狀態後，先存檔，再進入歌劇院。( 你時間很多的話可以先回去獸原找老鼠來學狂怒與收圖鑑 )</p>
<p>再次進入，第二次限時 5 分鐘開始，一樣用最快速度去啟動開關，衝到天花板區域，這次<span style='color: #ff0000;'>一進入天花板範圍就馬上打開選單，選擇「中斷」，返回標題畫面後再次進入</span>，開始觀察老鼠的移動空檔路線，找機會快速通過，萬一碰到老鼠，就按 Home 鍵離開遊戲，選「繼續」後會從剛剛進入天花板時開始。</p>
<p>重點來了，當你摸熟，躲過第一隻老鼠的路線後，<span style='color: #ff0000;'>在下一隻老鼠前「再次中斷遊戲」，重新「繼續」</span>，這樣即使第二隻沒躲過，重新繼續也不用再閃第一隻老鼠，接著就觀察第二隻老鼠的可能路線，再找出空檔閃過他。</p>
<p>閃過前兩隻老鼠後，後面的路線就照同樣的作戰方式，中斷、繼續來慢慢觀察並前進，不會浪費實際的限時 5 分鐘，而且一定可以閃過所有的老鼠，在時間內達到 BOSS 的位置。</p>
<p>如果你試了幾十次，怎樣也閃不過去，非要打個 1 ~ 2 場的話，集中攻擊打死一隻「黃色老鼠」即可，剩餘的綠色老鼠只要用魔法「治療」( 切換到敵人全體 ) 就會自行逃走，也能省下很多經驗值 ( 但後續的鐵道六連戰就要注意經驗值分配 )。不過在最低等級攻略+狂怒+圖鑑收集的前提下，建議是可以多花點時間去測試，有很大機會成功的，這感覺是比在獸原要找一個很少出現的怪物要快多了&#8230;&#8230;XDDDD</p>
