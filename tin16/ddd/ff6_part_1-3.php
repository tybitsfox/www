<p>嘎嗚加入後，會有相關說明，告訴你學習「<span style='color: #800080;'>狂怒</span>」的方式，簡單易懂，在戰鬥中使用「跳入」與後續歸隊時出現的怪物都可以學到狂怒技。</p>
<p><span style='color: #ff0000;'>( 嘎嗚在歸隊時，不用再給肉乾，也不要攻擊，過一下子就會歸隊了 )</span></p>
