<p>接下來當然就是花一點時間學習一些嘎嗚的「狂怒技」了。</p>
<p>&nbsp;</p>
<p>由於遊戲中狂怒技的種類有超過 200 多種 ( 據網友測試後實際可習得為 253 種 )，越後期在這裡會出現的敵人就會越多，相對的特定敵人的出現機率就會越低，而現在是第一次到獸原，遊戲中會出現的怪物，沒算錯的話，目前可收集狂怒技的怪物應該有 51 種 ( 有的不在怪物圖鑑內，有一般怪物也有首領怪物 )，若你有心要收集狂怒技，建議現在就先收集這 51 種。</p>
<p>&nbsp;</p>
<p>在戰鬥中嘎嗚使用狂怒變身後，每一種怪物都會有兩種攻擊，一是普通攻擊，一是特技，兩種攻擊方式是隨機出現，並且嘎嗚會在變身時完全成為該怪物的屬性，例如浮空、屬性吸收等等都是一樣的，在戰鬥時可以好好利用。</p>
<p>&nbsp;</p>
<p>目前可收集到的狂怒技 51 種裡面，其中有 4 個是沒有圖鑑編號的，另外 <span style='text-decoration: underline;'><span style='color: #000080;'><a href='./ff6_images/ff6_monsters/a112.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A112-重型裝甲</span></a></span></span> 是在之前 <span style='text-decoration: underline;'><span style='color: #0000ff;'><a href='./ff6_part_1-2.php#南費加羅鎮' target='_blank'><span style='color: #0000ff; text-decoration: underline;'>洛克篇／南費加羅鎮</span></a></span></span> 上駐守的裝甲兵，若你之前有對話進入過戰鬥，現在就有機會出現 ( 但機率還是偏低的，不過有遇過就一定會出現 )，而重型裝甲之後在納爾謝的雪原保衛戰還有機會可以遇到，不用擔心。</p>
