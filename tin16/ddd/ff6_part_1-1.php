<p><span style='color: #000080;'><strong>第三種方式：</strong></span></p>
<p>1. 連續三次去找巴南對話都回答「否」</p>
<p>2. 蒂娜再次回室內後，有士兵報告帝國進軍狀況</p>
<p>3. 士兵給予「源氏之護手」</p>
<p>4. 前往遺忘河</p>
<p>這三種方式的差別除了得到的飾品不同之外，第三種是沒有作戰會議事件的。雖然說作戰會議的有無並不影響遊戲的進行，但也是多看一些劇情，這邊推薦第二種方式來進行，原因如下。</p>
<p>「臂甲」只有在遊戲初中期較有明顯效果，雖然臂甲比較稀有，而遊戲中仍然不只一個臂甲，相較之下，「源氏之護手」的二刀流對正常版攻略之下，遊戲整體效益更好，除了可持兩把不同武器攻擊之外，若武器有附加屬性也同時有效，在遊戲後期的效果會很明顯，所以我個人推薦第二種方式，得到源氏之護手，並進行作戰會議事件。</p>
<p><span style='color: #008000;'><strong>★低等級攻略建議：</strong></span></p>
<p>1. 在這裡選哪一個飾品都沒差，反正一般的普通攻擊在低等級攻略中只是一個悲劇</p>
<p>2. 最後要離開這裡之前務必去道具店準備治療劑，建議最少 20 個以上</p>
<p>3. 由於巴南等級太低 ( 最低情況 Lv 2 )，可以讓其他人自殺，巴南一個人去地圖上練級</p>
<p>4. 大約讓巴南升到 Lv 7 ~ Lv 8 就足夠了，回旅館休息完後就可以正式出發</p>
<p>5. 出發前務必回去存檔，並且所有角色調整到後排</p>
<p>我用的是第二種方式，在最上面房間的士兵給的「<span style='color: #800000;'>源氏之護手</span>」</p>
<p>( 若你因為臂甲很稀有就選臂甲也沒關係的 )</p>
