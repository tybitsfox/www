<p>開場的介紹一定要來一張，當年看不懂的內容現在都懂了 ( 不過之前對岸早就有出漢化版就是了&#8230;. )，經典的遊戲再次重溫，感覺真棒！</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_1-1/02-narshe-001.png' alt='' width='486' height='326' /></p>
<p>當魔導裝甲在雪地中緩緩前進，熟悉的 Opening Theme BGM 響起時，心裡充滿了無限的感動，你是不是和我一樣把這首曲子聽到了最後呢？( 雖然點一下螢幕就會跳過，但是我不要~~~~ )</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_1-1/02-narshe-002.png' alt='' width='486' height='326' /></p>
<p>可以行動之後，直接往前走會遇到五場固定戰鬥，鎮上房子目前都無法進去的，往前走吧！</p>
<p>( 戰鬥都很簡單，隨便用魔導的火焰射線攻擊即可，若是低等級攻略請往下看 )</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_1-1/02-narshe-003.png' alt='' width='486' height='326' /></p>
<p><span style='color: #008000;'><strong>★低等級攻略建議：</strong></span></p>
<p>1. 女主角自殺：用女主角的「魔法 &gt; <span style='color: #800080;'>火焰</span>」燒了自己即可，兩個小兵去打綽綽有餘</p>
<p>( 是魔法，不是魔導，不然你會無法攻擊自己 )</p>
<p>2. 需要回復的話可用魔導的 <span style='color: #800080;'>治療原力</span></p>
<p>3. 現階段沒有物理攻擊可用，角色調整站後排即可</p>
<p>( 感謝網友 <span style='color: #0000ff;'>春日恭介</span> 補充 )</p>
<p>在女主角自殺的情況下，建議可以多打一些雜兵，賺一點錢以後備用，開場的這兩個跑龍套的等級不影響後續的低等攻略，多賺點錢也是好的。</p>
<p>&#8212;&#8212;&#8212;&#8212;&#8212;&#8212;&#8212;</p>
<p><span style='color: #ff0000;'><strong>★低等級攻略 補充說明：</strong></span></p>
<p>這次我會加入低等級攻略的參考原因很簡單，除了給想挑戰的玩家一些參考之外，在遊戲中後期取得魔石後，利用魔石的附加能力成長屬性，可以培養不同的角色能力，例如速度、力量、魔力….等等這些。</p>
<p>這部份其實與 FF4 的傳繼能力道具，在 70 級後提升角色能力的狀況有點像。你想想看，如果你 FF4 到了 Lv 90 才知道傳繼能力可以提升速度、力量、魔力、精神…等等，你只剩下 Lv 91 ~ Lv 99 共九次機會可以提升了，不是有點可惜嗎？</p>
<p>早期等期壓低一點 ( 並非一定要最低，有刻意去避免得到太多經驗值即可 )，對於中後期的角色培養會有很大的空間可以提升數值。</p>
<p>其實「低通」這兩個字在每個人心中所設定的條件不同，也許平均 Lv7.2 叫低通，平均 Lv 10 也叫低通，或是平均 Lv 15？不管是設定如何的目標，相較之下，都能稱為低等級攻略。</p>
<p>是否要去花時間求得最低等級，這還是看個人的目標而已，遇到無法迴避、逃跑的戰鬥，也並非一定要重新讀檔，更不用說要全圖鑑、全狂暴、全道具、全XX 之類的低通，是不是吃力不討好還是要看你自己設定什麼樣的目標。</p>
<p>這次寫攻略的方向，低等級攻略的建議只能算是輔助，也可以說是為了將來取得魔石之後，能夠提升更多的角色數值而準備，太自虐的低通打法個人也是不太推薦，那比較適合玩過無數次只為了刺激、成就感而玩的玩家。</p>
<p>以上，祝大家都能玩得開心XDDDDD</p>
