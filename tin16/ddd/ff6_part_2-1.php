<p><a name='孤島'></a></p>
<p><span style='color: #0000ff;'>孤島</span></p>
<p>世界崩壞後，場景來到一座孤島上，沉睡整整一年 ( 好扯&#8230; ) 的塞麗絲醒來，原來是西德一直在照顧她，結果弄到自己累翻，要塞麗絲去補魚&#8230;.. ( 照顧一年還不以身相許嗎XD )</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-1/01-solitary_island-001.png' alt='' width='486' height='326' /></p>
<p>走出小屋後往下走便能前往海灘捕魚</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-1/01-solitary_island-002.png' alt='' width='486' height='326' /></p>
<p>海邊就可以看到有魚可以補，基本上游得越快的魚品質越好 ( 確認方法下面說明 )，西德完全復原的機會也就越高，如果沒幾隻魚可以抓，可以在沒有抓魚的情況下直接回去找西德對話後，再來海灘找就會有變化</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-1/01-solitary_island-003.png' alt='' width='486' height='326' /></p>
<p>海邊的魚共有 4 種：<span style='color: #800000;'><strong>好吃的魚、普通的魚、難吃的魚、魚</strong></span>。要確實區分的話，可以在捕到魚之後，打開道具選單中的「貴重物品」，點一下「魚」查看下面說明欄就知道了 ( 如下圖 )</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-1/01-solitary_island-004.png' alt='' width='486' height='326' /></p>
<p>當然你想要西德活下來，就一直找好吃的魚或至少一些普通的魚給他，若你都是給他難吃的魚，或是說明只有寫「魚」的，那當然死得很快XD</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-1/01-solitary_island-005.png' alt='' width='486' height='326' /></p>
<p>西德的死活兩種結果都不會影響後續的發展，如果你有留存檔，想看看西德死活不同的狀況的話，可以試試，下圖是因為西德 GG 了，傷心不已的塞麗絲居然去跳崖&#8230;&#8230; ( 在演幻想世家嗎 )</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-1/01-solitary_island-006.png' alt='' width='486' height='326' /></p>
<p>跳崖後的塞麗絲大難不死，除了會在海邊發現帶有洛克頭巾的鳥之外，回到小屋就會看到西德留下的信件，告訴塞麗絲壁爐旁邊有地下室暗道</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-1/01-solitary_island-007.png' alt='' width='486' height='326' /></p>
<p>當然，如果你救活了西德，他會自己告訴妳並打開旁邊的暗道，但這種狀況下塞麗絲就不會發現那隻帶有洛克頭巾的鳥了</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-1/01-solitary_island-008.png' alt='' width='486' height='326' /></p>
<p>兩種狀況下，塞麗絲最後搭上木筏時的背景音樂也不一樣，一個悲傷悽涼，一個輕快啟航，不過當到達另一邊的世界地圖，上岸後就都恢復原狀了，有興趣想都看看的話可以找一下存檔。</p>
<p>另外要提一下的是，若你在孤島的時候走到外面去，一樣會遇敵，只是因為後續搭完木筏上岸後的週邊怪物都一樣，所以這時候不需要去外面找敵人來自找麻煩，趕緊捕魚觸發劇情就好了。而在日後取得全新飛空艇時，可以回到孤島的海灘上，撿到一顆魔石 ( 綠咬鵑 )，到時候會再提醒。</p>
