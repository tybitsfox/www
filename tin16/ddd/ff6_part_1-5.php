<div id='th0ths_quotes_sc_quote' style='font-style: normal; padding: 2px 10px 0px 10px; line-height: 1.5em; '>使用混亂加上煙霧彈時，要注意選擇魔法回避率較低的角色作為混亂魔法的目標，
因為如果混亂魔法Miss了就浪費了一個煙霧彈。當然先放透明再放混亂就一定行。</div>
