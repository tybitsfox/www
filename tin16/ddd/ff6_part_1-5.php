<p><span style='color: #800080;'><strong>一、混亂 + 煙霧彈</strong></span></p>
<p><span style='color: #ff0000;'>( Update: iOS 版本 1.0.3 版後「混亂+煙霧彈」方式已失效無法使用 )</span></p>
<p>A 角色對 B 角色使用魔法「混亂」，在畫面上出現魔法名稱時 ( 或出現滿天陸行鳥時 )，讓 B 角色開啟道具選單，使用「煙霧彈」，這樣就能在混亂情況下，丟出原本設定不能對敵使用的煙霧彈，迫使敵人撤退，戰鬥判定勝利。</p>
<p>這個方式雖然會讓 B 角色遭到「隕石」攻擊而陣亡，但不至於全滅，所以可以取得 10 點的魔法修煉值，要學魔法的時候要挑好人選，因為當下陣亡的人是不能獲得魔法修煉值的。這個方式在 SFC 版就行之有年，還有兩個運用之處，一個是之後的帝國會議上，另一個是在以後莫古要學習舞蹈技能的時候可以使用。要練魔法的話，煙霧彈就要多準備一些。</p>
<p>另外要注意的是，因為敵人是強制撤退，並非打倒，所以不會收得怪物圖鑑，但遇到過之後也會在獸原出現，以後要學狂怒就可以去獸原找他，至於打倒後才能收圖鑑，現在也可以打，不過以 GBA 版之後的版本 ( 包含現在的 iOS / Android 版 )，在現階段要實際打倒他相當困難，詳見第二個方法。</p>
<p><span style='color: #ff0000;'>Update:</span> ( 感謝網友 <span style='color: #0000ff;'>Lok</span> 補充 )</p>
<div class='th0ths_quotes_sc'>
