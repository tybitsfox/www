<p><span style='color: #0000ff;'>掌握獸原出現怪物的方式</span></p>
<p>有獸原遇敵表之後，當你確認好哪一個怪物還沒收到，就在獸原跑，直到該怪物的上一列出現後，結束戰鬥後，走到飛空艇旁，馬上選「中斷」遊戲，然後繼續遇敵，如果沒有出現你要的那一組怪物，直接退出遊戲 ( 勿結束戰鬥 )、重新繼續，坐上飛空艇到獸原右側 ( 比較近 ) 大陸，隨便遇敵後逃跑，回到獸原，再次中斷後繼續，看看有沒有出現該組敵人，若沒有，再重來一次，反覆操作之下，一定會遇到你要找的敵人。</p>
<p>簡單說明就是：</p>
<p>1. 確認你在順序表中，目標怪物的上一列</p>
<p>2. 停在飛空艇前方，中斷遊戲後繼續</p>
<p>3. 繼續遇敵 ( 看看是否有出現？)</p>
<p>4. 沒出現，戰鬥中直接退出遊戲，重新進入遊戲選「繼續」</p>
<p>5. 乘上飛空艇，到其他大陸上遇敵一次後逃跑 ( 右側最近 )</p>
<p>6. 返回獸原範圍降落，開選單，中斷遊戲，重新繼續</p>
<p>7. 繼續遇敵 ( 看看是否有出現？)</p>
<p>8. 沒有出現，就重覆上述 4~7 的步驟</p>
<p>停在飛空艇前方再中斷的意思如下圖，圖中右側大陸很近，所以你遇不到就在兩邊飛來飛去，中斷、繼續就好了，步驟不明白的話可以仔細看一下我上面寫的 1~7 點</p>
