<p><strong><span style='color: #800080;'>二、停止 + 物理攻擊</span></strong></p>
<p>沉睡獅 在崩壞前要實際打倒的難點有幾個，HP / MP 高達 32000 / 16000、自動隱形、全屬性吸收、幾乎全部狀態異常免疫、魔防物防都極高、會用隕石魔法 ( 全體 ) 反擊等等，不要說是低等級了，就算一般等級在崩壞前要打倒，少說也得 Lv 25 左右，而低等級之下在崩壞前要打倒更是難上加難。( 當然若是舊的 SFC 版本，使用嘎嗚的狂怒技：<strong>畝鼠</strong> ( 陷阱 ) 可以輕易打倒，或是直接送他一個 <strong>即死</strong> 魔法，但這些現在新版都已無法成功 )</p>
<p>而唯一沒有免疫的異常狀態是「<span style='color: #ff0000;'>停止</span>」魔法 ( 魔石 <span style='color: #800000;'>巨像兵</span> 可學到 )，可以利用這一點來有效地攻擊他，不過仍要花非常多的時間，光是 32000 的 HP 就可以磨死你，由於打起來實在太血尿了，我打了約十次沒成功就沒繼續試了，練魔法用混亂+煙霧彈就好，收圖鑑的話等後期收到強力裝備再說。</p>
<p>建議人選：<strong>埃德加、馬修</strong> ( or <strong>凱嚴</strong> )，另外兩人隨便</p>
<p>其餘兩人必要魔法：<strong><span style='color: #800080;'>隱形、停止</span></strong></p>
<p>建議裝備：前兩人主攻裝備巨人護手，且全員裝備赫耳墨斯之靴</p>
<p>選配裝備：龍騎士之靴 ( 主攻手兩人可考慮將巨人護手換成這個，原因後述 )</p>
<p>打法簡介：</p>
<p>1. 首先由另外兩人依序對敵人使用 隱形、停止</p>
<p>2. 怪物現身、被停止的狀態下，埃德加用鑽頭，馬修用爆裂拳 ( 或凱嚴用必殺劍 )</p>
<p>3. 攻擊 4 回合後，第 5 回合一定要馬上出「停止」魔法，繼續封住敵人行動</p>
<p>4. 重覆上述動作，主攻手每回合攻擊合計約 450，要打完 32000 要非常久&#8230;&#8230;</p>
<p>5. 因為停止效果恢復時無法知道，4 回合的時間是估算而已</p>
<p>6. 若你在第 4 回合就放停止，有時會發現無法打中 ( Miss )</p>
<p>7. 中途停止失效到再被停止時的瞬間，仍有極大可能被「隕石」攻擊，運氣好的話會有人存活 ( Miss )，運氣不好就直接全滅，也就是你失敗了&#8230;..</p>
<p>上面有提到選配 <span style='color: #800000;'>龍騎士之靴</span>，是考慮可以用「跳躍」來逃過隕石攻擊，要用的話就是在第 4 回合 ~ 第 5 回合之間跳上去，下來後通常敵人又會自己隱形，這時就先救人，再繼續上面的攻擊輪迴。但因為跳躍攻擊的攻擊力太低，所以不適合主攻，只能用來閃避。</p>
<p>依上面做法，是有機會在這時候確實打敗沉睡獅的，只是除了非常花時間之外，還要有過人的運氣，或者說人品要好一些XDDDDD，再者低等級之下，現在也沒有三星環 ( MP 消費變為 1 ) 可用，( 不過後續倒是可以選擇拿一個 <span style='color: #800000;'>黃金髮飾</span> 撐著用 )，勢必在連續的施法下仍然需要補 MP ( 或用 攝魔 來吸 MP )，這樣一來更考驗著玩家的耐心與鬥志。( 以太很貴耶XDDDD )</p>
<p>我現階段試了很多次，仍然都是失敗的，因為以後仍會在獸原遇到他，建議用第一種方式去學學魔法就好了，真要打倒他現在非常不適合，機會太小了，這只適合想挑戰的人去打，我的攻略還得繼續寫下去，等世界崩壞後再來收拾這傢伙。</p>
<p>( 當然，有其他方式也歡迎大家推薦，讓網友們測試看看低等級打法 )</p>
