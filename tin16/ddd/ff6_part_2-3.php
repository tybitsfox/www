<p><span style='text-decoration: underline;'><span style='color: #000080; text-decoration: underline;'><a href='./ff6_images/ff6_monsters/a178.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A178-大王花</span></a></span></span> 也算是稀有敵人之一，狂怒技「誘惑」其實有點強大&#8230;..</p>
