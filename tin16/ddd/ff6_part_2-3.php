<p>走下來兩側各有一幅畫，右邊是個門，不過無法進去，是在後續有個門進去會從這裡掉出來；而左邊的椅子畫像一靠近就會遇敵，不過只要小心一點繞過去就 OK，同樣的敵人路上也會遇到，繼續往左上前進</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-3/01-jidoor-014.png' alt='' width='486' height='326' /></p>
