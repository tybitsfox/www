<p><span style='color: #0000ff;'>準備事項</span></p>
<p>接下來我們可以先去找瑞姆 ( 最低等級為 Lv 6 )，但是因為瑞姆所在地點吉多爾鎮有無法回避的經驗值，當然我們就先把「經驗值吸收器」嘎嗚給找回來比較妥當。當然這是為了個人目標而言，如果你沒那麼在意，也不需特地去找嘎嗚歸隊就是了。另外要注意的是，因為嘎嗚會升級，所以可以準備一下幫嘎嗚裝備提升能力的魔石，自行決定即可。</p>
<p>先來到獸原 ( 地圖右上方 )</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-3/01-jidoor-001.png' alt='' width='486' height='326' /></p>
<p>以三人隊伍在獸原遇敵 ( 不用給肉乾，也不要攻擊 )，嘎嗚出現一會兒就會加入 ( Lv 16 )</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-3/01-jidoor-002.png' alt='' width='486' height='326' /></p>
<p>接著去賺足夠的錢 ( 約 12~15 萬左右 )，賺錢的話到馬蘭達鎮附近的小沙漠打仙人掌 ( 請看上一篇 )，然後去地圖最東南方角落的薩瑪莎村買 10 根左右的 <span style='color: #800000;'>神聖棒</span> 備用</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-3/01-jidoor-003.png' alt='' width='486' height='326' /></p>
