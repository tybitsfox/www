<p><span style='text-decoration: underline;'><span style='color: #000080; text-decoration: underline;'><a href='./ff6_images/ff6_monsters/a179.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A179-靜畫魔</span></a></span></span>／弱點火／HP 4889／MP 390 <span style='color: #ff0000;'>( MP 0 即死 )</span>，因為有經驗值 2331，所以戰鬥結束前除了嘎嗚之外一樣都要自殺，打法上用火魔法或是吸他的 MP 都可以很快結束，打完後嘎嗚獨吞 2331 經驗值，升到 Lv 17，差 263 點到 Lv 18。戰鬥結束後會出現一道新的門，直接進去吧！</p>
