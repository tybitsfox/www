<p>接著繼續繞，在左邊有一幅格斯塔拉皇帝的畫像，「連續調查 2 次」後可以取得皇帝的信，不過信的內容就是告訴你鳳凰洞窟的情報而已，我們已經去過了XD</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-3/01-jidoor-009.png' alt='' width='486' height='326' /></p>
<p>最後到左下角的女人畫像調查：<span style='text-decoration: underline;'><span style='color: #000080; text-decoration: underline;'><a href='./ff6_images/ff6_monsters/a177.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A177-吐霧女法師</span></a></span></span> x2／HP 3580。這一場無法逃走，低等級攻略之下，戰鬥前讓嘎嗚裝備 <span style='color: #800000;'>緞帶、反射指環</span> 即可，開戰後其餘三人自殺，嘎嗚一人應戰，變身「<span style='color: #800080;'>機車大魔</span>」( 清單中第一個 ) 很快解決，嘎嗚這一戰還不會升級，維持 Lv 16。</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-3/01-jidoor-010.png' alt='' width='486' height='326' /></p>
<p><span style='text-decoration: underline;'><span style='color: #000080; text-decoration: underline;'><a href='./ff6_images/ff6_monsters/a177.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A177-吐霧女法師</span></a></span></span>，無法逃跑，經驗值 1151&#215;2 = 2302，低等級攻略建議都讓嘎嗚吸收，弱點是毒，一般攻略也是裝備反射指環、緞帶等，直接吸她 MP 或用毒屬性魔法應戰都可以。</p>
