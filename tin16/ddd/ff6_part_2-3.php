<p><span style='color: #0000ff;'>歐澤的公館</span></p>
<p>接下來前往地圖西側陸地的吉多爾鎮，隊伍人選的話除了嘎嗚要在之外，其餘隨便XD</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-3/01-jidoor-004.png' alt='' width='486' height='326' /></p>
<p>直接前往最北邊的大房子：歐澤的公館</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-3/01-jidoor-005.png' alt='' width='486' height='326' /></p>
<p>上樓梯旁的壁燈可以打開室內的照明</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-3/01-jidoor-006.png' alt='' width='486' height='326' /></p>
<p>左側的壁畫藏著戰鬥，首先是最右上的花：<span style='text-decoration: underline;'><span style='color: #000080; text-decoration: underline;'><a href='./ff6_images/ff6_monsters/a178.png' target='_blank'><span style='color: #000080; text-decoration: underline;'>A178-大王花</span></a></span></span> x3／HP 2200，只有這裡會出現，戰鬥是可以逃跑的，要收集狂怒技的話這裡一定要開戰，要避免經驗值的話就逃跑，不介意的話用火魔法攻擊即可，小心敵人的「誘惑」攻擊，裝備緞帶可以預防。</p>
