<p>打完後會出現一道門，整理一下隊伍狀態後直接進入</p>
<p><img class='alignnone' src='./ff6_images/ff6_part_2-3/01-jidoor-012.png' alt='' width='486' height='326' /></p>
<p>往左下樓去 ( 注意會開始遇敵，要收集的話參考前面列表 )</p>
