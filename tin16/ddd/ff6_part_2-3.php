<p><img class='alignnone' src='./ff6_images/ff6_part_2-3/01-jidoor-015.png' alt='' width='486' height='326' /></p>
<p>接著來到有三道門在變換的地方，先往上再往左到盡頭有藏一個寶箱 ( <span style='color: #800000;'>亡者戒指</span> )，再回來等最左側的門開啟時進入 ( 中間和右邊的門進入會從剛剛上一段的門畫像掉出來重走 )</p>
