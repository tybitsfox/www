<br>
　　<br>当你把村子扫荡一遍之后，剧情应该也进展得差不多了，主角一行在屋里对话后，下楼和长老对话，然后再上楼回房间，选择休息触发剧情。下楼，1楼的龙族青年表示主角一行可以来他这里休息。
<br>
　　<br>进入最终迷宫，等级不高的队伍在这里会体会到“打野怪如过BOSS”的感觉，这里的野怪攻高血厚，不少甚至还会使用拉普松的技能，而且攻击一点都没掺水，着实让人X疼。
<p style='text-align: center;'><img alt='' src='./171011/334-1G011135542-50.jpg' style='display: block; margin: 10px auto; max-width: 670px;'></p>
这地图一样没有地图，还好不算复杂。进迷宫之后，先回身确认一下自己进来的入口，然后摆正方向——这时候，右手边的路一直走，见分叉不转弯，一直走到底，能拿到“危险紧身衣”，可以和光之裙合成“神秘紧身衣”。回门口，左手边是冤枉路，咱们走中间这条。环形楼梯下去，宝箱里有小徽章，出门遇到两个岔路，走右边。沿着透明楼梯上楼，又发现三条岔路(我的队伍从这里开始遇敌了，点蚊香失效，但这里可以遇到金属王)。从左边那条路开始走，走到头拿到小徽章一枚。返回起始点，走中间的路，中途能看到一个独立的浮岛，上面的宝箱里是奥利哈尔钢。右手边龙头骨旁的宝箱里能拿到骷髅头盔，第二个战鼓的材料有了，合不合大家自己拿主意吧。
<p style='text-align: center;'><img alt='' src='./171011/334-1G011135543.jpg' style='display: block; margin: 10px auto; max-width: 670px;'></p>
