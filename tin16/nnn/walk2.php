	<br>贝尔加拉克赌场：祈祷戒指1000枚(1500G)、亮片长裙3000枚(2350G)、圣者之灰5000枚(150G)、隼之剑10000枚(5000G)、失散金属之凯50000枚(4750G)、古林加姆之鞭200000枚(10000G)
