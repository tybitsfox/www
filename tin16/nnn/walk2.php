　　<br>神鸟升空，进入魔王城，此处怪较凶猛，以绿巨魔和大恶魔为主，防高血厚，大恶魔还免疫火系和光系魔法，一路打过去颇为费时间，难搞的野怪外加高遇敌几率能让人抓狂。此处建议先不进魔王城，去史莱姆山或者神鸟巢穴把平均等级刷到50级以上，这样在魔王城里开着“特洛艾斯”就可以达到不遇敌——况且，这里的BOSS，不到50的队伍也很难啃得下来。磨刀不误砍柴工，可能就是这个道理吧。
<p style='text-align: center;'><img alt='' src='./171011/334-1G011135534.jpg' style='display: block; margin: 10px auto; max-width: 670px;'></p>
很多人都说魔王城难走，其实只要拿到了地图(进门左手边一直走，会看到左右两个门，进左边的，对面的门出去可得)就完全没什么可难的了。宝箱也没必要都拿，等这段剧情过后，戈尔德会恢复原状，宝箱都会保留下来。开着“特洛艾斯”跑吧，好东西还是不少的，木桶阵是一定要砸光的，里面藏着奥利哈尔钢。找到一个有瀑布的天厅，一层一层往下走，瀑布底部有里城入口。
<p style='text-align: center;'><img alt='' src='./171011/334-1G011135535.jpg' style='display: block; margin: 10px auto; max-width: 670px;'></p>
进入里城，门口会发现4个主角的雕像，四周类似普通的街道但一个人也没有。接下来围着这里绕圈吧，绕着绕着就会发现景色慢慢变了，最后来时的路变成了新的入口，进入触发BOSS战。
<br>
　　<br>这场BOSS战有些难度，主要在于BOSS的痛恨一击。如它两次同时瞄准一个目标，并且其中一个技能为痛恨的话，库鲁鲁和洁西卡是100%被秒的。而且BOSS高概率使用冷冻波，如果觉得被打断蓄气太频繁，就20气出手吧。
<br>
　　<br>击败BOSS后，失去魔力源头的魔王城开始崩塌，此时不能使用“利米雷特”直接出去，掉头原路返回，回环线继续绕圈吧。绕到中途会触发一次战斗，一只魔王使者带两个火元素，这里批很好打，如果身上带着冠军队伍且打之前那只大萤火虫没减员的话应该直接能A掉。继续绕圈，绕着绕着发现一个往上的楼梯，别上当，那是假的，继续绕，一直绕到4个主角雕像向你宣战为止。
<p><span style='color:#b22222;'><strong>四雕像战</strong></span></p>
<br>
　　<br>4主角雕像打起来稍难，会使用群体高级雷、冰魔法。4雕像打掉后上楼梯，顺进来的路一直往回走，上到一半会从喷泉里出现两条魔鲨鱼向你开战(明明是煮熟的海龙嘛)，这两条东西攻击很高，咬上就是痛恨一击，所幸血不多，一轮蓄力就能打掉。
<br>
　　<br>逃出城堡，再次触发BOSS战，说起来这东西也不算BOSS，一动，也不会冰冻波，几回合弄死之后触发剧情。
<p><span style='color:#b22222;'><strong>七贤者之魂</strong></span></p>
一连串剧情之后，玩家踏上寻找七贤者之魂的旅程——很简单，其实就是去七贤者死去的地方，剧情记得熟的朋友甚至用不到笛子，笛子只是用来寻找位置的工具，贤者之魂玩家只要直接调查闪光点就可以拾取。忘记的朋友请不按顺序前往：
<br>
特拉佩塔贤者小屋的废墟上找到仙紫光珠
<br>
东塔顶层(洁西卡哥哥被杀害的那个塔)找到天蓝光珠
<br>
玛伊艾拉修道院院长的房间里找到黄金光珠
<br>
贝尔巴拉克兄妹大屋二楼左手边房间的沙发旁边找到土黄光珠
<br>
利普尔亚奇咒术大叔的院子里找到叶绿光珠
<br>
梅蒂婆婆家后院的山洞口找到红莲光珠
<br>
法皇馆2楼棺材前面找到白银光珠(旁边有个NPC很讨厌，拾取的时候注意角度)
<a name='ax15'><p><span style='color:#b22222;'><strong>最终战</strong></span></p>
 　　<br>七个魂石全部收集齐之后，回到雷缇亚斯，面临最终战，把好东西全都换上吧，珍贵道具也不要吝啬——不过记得每个人身上留一个格子，然后确认一下战队队员都复活了，最后从神鸟栖木下方穿过触发剧情。升上高空，对战一切灾难的源头。作为DQ系列最终BOSS战史上的第一场空战，8代的最终BOSS无论是体积还是身上肥肉的比例都是那么气势恢宏。玩家一行就算乘坐神鸟依旧显得小的可怜。
<br>
 　　<br>BOSS战·暗黑神拉普松(第一阶段)，这个形态任何攻击对BOSS无效——包括减防等Debuff技能，所有队员需共同使用刚得到的法杖，选择“祈祷”——记住是共同使用，哪怕缺一个人没用祈祷都会失败。祈祷后出现一个贤者之魂，下一回合可以加血，加防，法师开盾，然后再集体祈祷一回合。如主角加出贝荷马珍(全员回满血)，那么这BOSS能好打N多。七个贤者之魂全叫出来之后，进入第二形态。
<br>
 　　<br>BOSS战·暗黑神拉普松(第二阶段)，破盾后的拉普松开始向主角一行全力输出，无论是流星雨还是全员的咸猪手威力都是不减当年，2动的拉普松已经不是全员+100HP这种魔法能补得回来的了，如果主角未点出贝荷马珍，那么建议佩戴贤者之石辅助奶骑群刷。模拟党的队伍平均50级就能打掉拉普松，每回合都能存取档简直是作弊器一般的存在。
<br>
 　　<br>BOSS死掉之后自然是天下太平，皆大欢喜的大团圆，国王和公主变回原样——猥琐绿的原样……呃……还是不说了，大家自己看吧。
<p><span style='color:#b22222;'><strong>后续</strong></span></p>
<p style='text-align: center;'><img alt='' src='./171011/334-1G011135541.jpg' style='display: block; margin: 10px auto; max-width: 670px;'></p>
镜头一转，主角又接到了护送公主去参加2货王子婚礼的任务，去2楼公主的房间触发任务，接下来就是一系列剧情+剧情+剧情了。因为毫无难点，玩到这里的各位也不会被后面剩下的一点点剧情卡住，所以我就不写了。皆大欢喜是DQ系列历来的结局，至少至今还没有例外过——我觉得这样才好，有缺憾的美毕竟不是真正的完美。(恶龙呢?恶龙呢?明明叫勇者斗恶龙!怎么最终BOSS是这么一个猥琐的死胖子?说好的恶龙呢?……别急，请往下看……)
<a name='ax16'><p><span style='color:#b22222;'><strong>二周目</strong></span></p>
通关画面要看完，走字幕也要看完，这个过程中可以回顾一下那些曾经的剧情CG，最后会提示存档，一定存上——大家要依靠这个档进入二周目。
<br>
　　<br>重新引导CDVD，然后立刻找旅馆睡一觉，会梦到一座祭坛。还记得拉邦屋东边的神龙祭坛吗?开飞行模式飞过去吧。调查龙形雕像进入隐藏迷宫。
<br>
　　<br>二周目隐藏迷宫里怪物很强，但如果你平均等级50+，开“特洛艾斯”应该可以达到不遇敌(我到这里的等级是52、53、51、50，点蚊香不会踩到怪)，进一个很有特征的大门后遇到一个岔路，左边是死路，但可以拿到一个散失金属铠甲，大家应该用不到了吧，右边的岔路时下去的楼梯，下去之后又是岔路，这次左边依旧是死路，可以拿到技能之种。回去顺另一条路走，走着走着前方又是岔路，这次先走右边，可以拿到死神之盾，用圣者之灰洗一下能得到女神之盾。返回之前的路，一直走，下楼。过天桥，来到一个都是毒沼的屋子，右前方有个宝箱是“潘多拉的盒子”(最高级宝箱怪，不知道掉啥，反正我用杨胖子偷了半天啥都木有)。再往前走进入室外石桥场景，中途左转岔路里的宝箱是精灵药水，右转的岔路则是金属王之枪，这可是一周目拿不到的好东西，非枪系男主角可以用它和风之刃合成最高级回旋镖。回主路继续前行，踩传送点进入下一层。一直前行，经过一个坟墓后，遇到岔路，这次走左边，右边是死路。
<p><span style='color:#b22222;'><strong>龙神村</strong></span></p>
一路前行，来到龙神村(日文：竜神族の里;日文：Dragovian Sanctuary)
<br>
