	<br>答案很明显了吧，另注意兑换道具时不要超过999个。
<br>
	<br>好了，身上有钱感觉底气十足，继续我们的旅途吧。鲁拉，莱顿之塔，然后向西北方向走，穿过隧道，来到雪之国
