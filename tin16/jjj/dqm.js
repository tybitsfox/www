  function getVl(xl) {
    var s = "";
    if(xl[3]==0) {
      s = "?";
    } else if(xl[3]!=7) {
      if(xl[3] & 1) s += ",GB1";
      if(xl[3] & 2) s += ",GB2";
      if(xl[3] & 4) s += ",PS";
      if(s.length>0) s = s.substr(1);
      if(s=="GB1,GB2") s = "GB1+2";
    }
    if(xl[4]) s = xl[4] + " " + s;
    if(s=="") s = "&nbsp;";
    return s;
  }
