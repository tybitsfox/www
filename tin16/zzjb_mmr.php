1、拉多镇攻略 <a name=mmr_002></a>
被老爸赶出家后，先到保管处把“毛巾”（现在用不上）存了，而后在旁边的自动售货机上买四个参丸和两个手雷（你的50G只够买这些了），有机会重奖哦，
记录处里也有一台一样的自动售货机，奖品是“火瓶”。之后到左边的纪录处做一下游戏纪录。 
这你就可以出镇子了。哗！外面的世界真精彩！ 
拉多镇周围会遇到巨蚁、杀人虫、火焰枪、仿生蜗牛、加农炮、生物炮等怪物，先用弹弓打打小怪物，挣点经验值（EXP）和金钱（G）。HP少了可以回家和姐姐
说话后睡一觉，如果离镇子太远了怕回不去，就使用参丸补充HP吧。升个一、二级后，你的HP、攻击力、防卫力、速度、体力、战斗力、智力等等，都会大大提高，
这样你就可以到拉多镇北边的山洞去了。出拉多镇，沿山边顺时针方向走，在拉多镇北边有一个山洞，进洞后，还是沿顺时针方向走，在尽头有一地洞，下去到地
下二层往左上方走，有一个箱子，调查，会得到一枝短枪，装备上，嘿嘿！小怪物都不是你的对手了。 
原路返回，从拉多镇往南走，有一个大坑，进去后一直往左走下到地下二层，再往上、往右走，而后往下、往左走，在尽头就会遇到战狗，对话后防御一下
（HP要大于30，如果你的HP大于60，也可以攻击，只是没什么作用），第二轮红狼会出现帮你把战狗干掉，红狼和你说完话后你就能得到NO.1战车了。 
坐上战车，给战车起好名字（这之后任何时候如果主角被怪物打死了或战车被打的严重损坏不能动了，拉多镇就会出现一个战车出租店），出洞回拉多镇，给战
车补充满弹药、装甲片，再到拉多镇北面山洞，进洞后往左走到地下二层，往上继续往里走到地下三层，继续前进会有一个岔路口，从左边走，在一个箱子前有
陷阱，连人带车掉到地下四层，这里就是第一个通缉犯“水怪”（奖金1000G，要到勇士办事处去领）的老巢，随便走走，很容易遇到水怪，这可是第一场硬仗，用
你的45主炮和水怪们磨吧，应该不会有什么问题。打败水怪后回到地下三层，记着从另一方向拿上木箱里的07机关炮。而后返回地面，继续向北就是关卡，进去
和里面的人说话后就会让你出关了（必需找到NO.1）。<a href=#mmr_001>返回</a> 

2、麦基镇攻略 <a name=mmr_003></a>
出关卡后往东，不远处有一大片被大树围起来的大草甸，战车不能进入，在草甸南边可以看到麦基镇，下车可以进入。 
一进镇子，有一片菜地，耕会儿地吧，你会找到一枚戒指，到上边的屋子里和一个女人对话，她会用宝石镜换你的戒指，这可是制造激光炮的第一片镜片哦。
到人类武器店里买上几个火瓶，以后会用得上的。出麦基镇向西8步、向北13步，调查，可得到近2000G！一笔不小的财富啊，一定要记着拿哦。 
回麦基镇后一路向南，走到海边会看到有一座房子，进去会有些补给可买。出房子向西来，有一小片被四丛小树包围着的空地，下车进入，这是第一个商人
的秘密营地，没什么值得买的好东西。往回走，在东边的大山的中间，有一条山涧小道，下车可以步行进入，顺小道一直往里走，有一片小树林，西边还有一所
房子，这里是隐退的勇士们聚居的地方。和一个老太太对话后能免费休息（可补满HP）。在小树林里和山涧小道上，会遇到大量的金蚁，很值钱的（每只210G），
别的武器很难对付，只有火瓶好使，打上几只挣点钱吧，要是你的G不太够的话。小树林往东北方向也有一条山涧小道，走进去在见到的第一棵大树旁调查，会
得到一样终极武器（波布镇以前）----长枪。西南方向也有一条小道，出来就能看见波布镇了。不过别忘了你的战车还在大山的西边呢。 
返回麦基镇旁，向东、再向南，就来到了最大的镇子----波布。 <a href=#mmr_001>返回</a>

3、波布镇攻略 <a name=mmr_004></a>
进入波布镇后，先到处转转吧，熟悉熟悉情况，而后，出镇向西南方走，不远处有一所房子，进院子后要下车才能进入房间内（记住一定要坐自己的战车来），
你一进屋，有一个小男孩迅速跑出来，跳上你的战车开上在院子里转圈圈，他就是你的第一个同伴----修理工。和他对话，同意加入（不同意的话他是不会把战
车还给你的），你就有了第一个同伴。回到波布镇，在战车店里将NO.1换装一下发动机和主炮，买上传真，再到战车出租店里给修理工租一辆战车，补满装甲片
和弹药后就可以出镇找NO.2战车了。出波布镇向东，走到海边有一座大桥，过桥（可能碰上第二个通缉犯：水鬼，最好“逃跑”，否则么…）后有一座工厂，上楼
找到社长对话后可得到NO.2。传真回到波布镇，补充好后再来大桥上找水鬼，有NO.2干掉水鬼（奖金3000G）就轻而易举了。 
再回波布，给NO.1买上10发铁弹，给NO.1、NO.2各买几个PAG 40，而后，出镇一直向北，过一座小桥就是巨型炮封锁区，冒着炮火继续前进，在巨型炮阵地前的
小桥上用PAG40补充装甲，再前进一步即可与巨型炮决一死战了。先用铁弹攻击，巨型炮被铁弹击中后（多打几发）会“装备损坏，无法攻击”几乎不能攻击你了，
再用NO.1的55主炮、NO.2的格林副炮（攻击一组）攻击（还有一样人用工具：烟雾，作用也很大哦，学会使用，战斗中会有很大帮助），十来轮后即可消灭巨型炮。 
灭掉巨型炮后，如果战车的某些装备被彻底打坏，可传真回镇子到修理店修理（要花很多G），也可回拉多镇找老爸修理（不用花G）。 
过巨型炮阵地向东，路南边有一所房屋，是商人的大本营，没到过奥多镇是进不去的，其实这里也没什么可买，只有SOL1还算不错。往北不远就是奥多镇了。 
