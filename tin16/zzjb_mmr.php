4、奥多镇攻略 <a name=mmr_005></a>
进入奥多镇后，到左上角的房子的地下酒吧里先找到第二个同伴：女战士。这时主角或修理工的HP必须有一人的大于255，否则女战士是不会理你的。而后，到
东边的房子里的自动售货机上购买炮弹，中奖后的奖品是“军号”，多买多中。这可是“夺命三宝”的第一宝物。记得以后“军号”少了就来这里买炮弹啊。 
给女战士租一辆战车，而后从奥多镇向南来，到商人的大本营附近，再向东，就到了臭名昭著的帕特港。先到东南角的房子二楼最里间打败异形
（记得用“军号！”）取得“扳手”，再到西南角的房子里，这里有NO.6在出售，可价钱很高，大概要50000G—100000G不一定，看你的运气了。买不起就先放一放
吧，等你有了足够的G随时可以来买。到二楼，有“党参”可买，这可得买几个。再往西走，还有一座房子，进去后（要使用扳手）从下水道钻到另一房间，可找
到一样武器“火焰枪”，一定要拿哦。印象中（不很准确）到奥多镇后，使用时空传送机，就有可能出现故障了，这样你就会被送到波布镇东北边的“吉田生命研
究所”，里边的计算机上有再生丸可拿。传真回到奥多镇，给战车补充完毕后出镇向北，不远处有两座桥，过东边的桥后往北20步，再往东20步，调查可得近
5000G。再回到原路继续向北就到罗克镇了。 <a href=#mmr_001>返回</a>

5、罗克镇攻略 <a name=mmr_006></a>
进镇子后，先到人类装备店倾你所有，改装三位勇士的武器和防御装备，而后买几个酸瓶，坐战车出镇向西、向北，有一所房子，这就是第三个通缉犯“马歇尔
”（奖金10000G）霸占的“无敌医院”。下车步行进入，上到二楼，在里间能遇到马歇尔，一直跟着追过去，从另一面下到一层，马歇尔会开着“拦截车”来和你开
战，先用“军号”，再用“酸瓶”，几回合后马歇尔就会被活活烧死，哈哈，有意思。有时候马歇尔也会上“拦截车”，大概是“打掉针”补血去了，如果你不能把“拦
截车”打烂，几回合后马歇尔还会从“拦截车”上下来，这时他的HP又补满了，还得再用“酸瓶”，直到干掉他。 
打败马歇而后，就得到NO.3战车了，这辆救护车有个特殊作用：自动补HP，谁坐在这辆战车上每走一步都会补1HP，直到补满为止。出医院传真回罗克镇，到酒
吧的某一个位子上连喝三杯酒后，旁边的小姐就会过来邀你一起喝酒……。到战车装备店改装一下战车，记得要买个“尼克”（C装置）备用，补充完后出镇往东走，
往东北，有一条林间小道，进去在最北边有两棵大树，绕着这两棵大树走个来回，再到海边，潮水就落了（这是最根本的方法，不要听信有些人的什么秘籍、
绝招之类的废话），露出一个洞口，进洞一直往左走，左下角有个箱子，里面是09机关炮，左上角是出口，出来后往西，有一所小房子，可补充一下，再往北，
就可远远望见弗里镇了。  <a href=#mmr_001>返回</a>

6、弗里镇攻略 <a name=mmr_007></a>
弗里镇是个被冰冻的镇子，进大楼的路只有一条，挺难找的，要好好用一下冰面里的那两棵树。另外，进大楼有一绝招：在冰面随便什么位置滑行的时候，点一下
A键，出现选项框后，选取“装备（“对话”不行）”等任意项目，而后退出，你就会停留在原地，然后想往什么方向走就都可以了（用这个方法可以不打大象就拿
到象牙）。在大楼的最底层，有第四个通缉犯“大象”（奖金10000G）。打大象的方法有很多，最有意思的是吹一遍“军号”后，如果大象再“冲撞”，就会活活把自
己撞死！如果此时不打大象，到后来你的等级较高了，防御装备也是最好的了再来打大象的话，不用吹“军号”大象也会撞死自己。其实，即使是你刚打到弗里镇，
只要你把此镇之前的所有最好的防御装备都装备上，大象冲撞时也会自己掉血，只是掉得太少了。 
在弗里镇有两样工具一定要买：“药箱”和“探测仪”，药箱是用来给人补HP的；探测仪是用来寻找埋在地下的金属物的。 
弗里镇西南方有第二个商人的秘密营地，这也是第一个战车能开进去的秘密营地。这样的秘密营地总共有四个，奇怪的是如果你把战车停在其中的某一个秘密营
地里，在其它任意一个秘密营地里都可以找到哦。 
从弗里镇往东来，过一座桥后不远处有一座房子，进去后在房子的后面有一个地洞，在地洞的最里面可以找到一门105加农炮！从房子里出来继续往东走，在海上
有一条小道直通一个海洞，在洞里用探测仪可以找到“V24发动机、尼克、钢盔、PAG80和NO.5战车”。从洞里出来沿原路返回到岛上，向北绕过海角，在最北边有四
棵大树，在第二颗大树处面向海调查，可得到5000多G。然后一路向西去，过两座海岛后就是光环大楼了，进楼后追着一个小女孩说话，而后到楼顶和女孩的父亲
说话，他就会帮你把大楼炸掉，填平海沟，你就可以继续向西来到伊尔镇了。 <a href=#mmr_001>返回</a>

7、伊尔镇攻略 <a name=mmr_008></a>
伊尔镇里有个著名的“铁洞”，是专门改造战车的地方。在这里可以改进你的每辆战车装备，不过，最好还是根据具体需要量G而行。伊尔镇里的武器装备并不怎么
好，不过，歌厅里倒是挺好玩...。如果你的G不够用的话，这里有个挣钱的方法：到人类工具店里的自动售货机上买参丸，中奖的奖品是“金棒”，一个就值1000G!
你可以多中几次啊，能发一笔不小的财呢。 
从伊尔镇往南走，到河边后往西不远就是北码头了，进码头前有一片地雷阵，有一条道能绕进去，用探测仪自己慢慢摸索吧。这里是瓦鲁霸占的地方，先不要进
去，再往西走走，有一个小镇，就是无歌村了，这里的武器装备还说的过去，打帕鲁能用得上，在这里倾你所有改装你的武器装备吧，而后再来到码头和瓦鲁一
决生死。瓦鲁三个手下的催眠波是很讨人烦的，一定要先将这三个小兵解决掉，之后解决瓦鲁就易如反掌了。打倒瓦鲁后乘船过河来到南码头，出码头后往东四
步，再往南走九步，调查，能得到一台特殊“V48”发动机，载重52T，并可改进为终极发动机V100！不过这得可要你数万G，52T已经足够用了，还是以后再改吧。 
继续往东走，绕过山脚向南，不远处就是索鲁镇。 <a href=#mmr_001>返回</a>

8、索鲁镇攻略 <a name=mmr_009></a>
进索鲁镇后往右拐，能看见一辆装甲车，这就是你的NO.4，不过有个人挡着你的去路，只有打败下水道里的异型鱼他才会把战车给你。下水道的入口在镇子的左上
角，在井盖上调查，进入下水道，里面的怪物全是“水鬼”，很厉害的！这时你必须多多准备“军号”和“党参”，一般此时主角的速度要快于水鬼，吹过军号后就不怕
水鬼的攻击了。打倒水鬼可随机拣到一样特殊工具“药液”，这可是仅次于攻击力500的激光炮的好东东啊，一定要多打几个水鬼，能得到一两个药液，对以后的作
战有极大的帮助哦。在下水道的中间有一台计算机，打开后下水道里的水就没了，但水鬼也没了，以后就再也找不到有如此之多的水鬼打了。放了水后，异型鱼就
趴在地下，过去和它对话，然后开打，它不是什么厉害怪物，几回合就可以揍扁它。其实在一进下水道不远处有一个台阶，下台阶面向水说话，异型鱼就会出现。
