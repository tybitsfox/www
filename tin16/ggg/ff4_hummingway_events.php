<p><a name='彩虹布丁'></a><br /> <span style='color: #ffffff;'>-</span></p>
<p><span style='color: #ffffff;'>-</span></p>
<p><strong><span style='color: #0000ff;'>取得彩虹布丁</span></strong></p>
<p><span style='color: #ffffff;'>-</span></p>
<p>彩虹布丁在各種顏色的布丁系怪物 ( 膠質怪、看起來軟軟的像布丁一樣的就是了 ) 身上大多都有，但掉落的機率都是身上四種物品裡最低的，也就是一般稱做「第四檔」掉落物品。所以一般在命名威的全事件中，除了最後的隨機地點之外，大概都是卡在這裡最久，其他的事件幾乎不用花時間，只要找到他，然後對話就行了。</p>
<p><span style='color: #ffffff;'>-</span></p>
<p>而唯一「第三檔」掉落 ( 代表機率較高 &#8220;一點點' ) 彩虹布丁的就是有名的「<span style='color: #800000;'>布丁公主</span>」，出現在最終迷宮「月面地下溪谷」的 B5 右下角的小房間內，使用<span style='color: #800000;'>報警器</span> ( 哼鳴威一族的住所有賣，一個 $3000 ) 後叫出。</p>
<p><span style='color: #ffffff;'>-</span></p>
<p>月面地下溪谷 B5 右下小房間，進去後使用報警器即可遇到</p>
<p><img class='alignnone' alt='' src='./ff4_images/ff4_hummingway_events/01-hummingway-033.png' width='468' height='351' /></p>
<p><span style='color: #ffffff;'>-</span></p>
<p><span style='color: #ffffff;'>-</span></p>
<p><span style='color: #0000ff;'>布丁公主</span></p>
<p>HP：<span style='color: #0000ff;'>10000</span></p>
<p>掉落物品：(1)特級以太、(2)聖靈藥、<span style='color: #800000;'>(3)彩虹布丁、(4)粉色尾巴</span></p>
<p>技能：公主之歌 ( 全體狂暴狀態，只有塞西爾的水晶裝備能防 )</p>
<p><img class='alignnone' alt='' src='./ff4_images/ff4_hummingway_events/01-hummingway-034.png' width='486' height='326' /></p>
<p><span style='color: #ffffff;'>-</span></p>
<p><span style='color: #ffffff;'>-</span></p>
<p>布丁公主身上還帶有第四檔掉落的「<span style='color: #800000;'>粉色尾巴</span>」，所以一般的攻略都建議要取得彩虹布丁的話，就直接在月面地下溪谷打布丁公主就好，這樣也可以順便刷粉色尾巴，去交換最強防具：<span style='color: #800000;'>精金鎧</span>。</p>
<p>( 關於 FF4 後期的強力武器裝備，請參考：<span style='text-decoration: underline;'><span style='color: #0000ff; text-decoration: underline;'><a href='./ff4_weapons.php' target='_blank'><span style='color: #0000ff; text-decoration: underline;'>[專題] FF4 尾巴與強力裝備收集</span></a></span></span> )。</p>
<p><span style='color: #ffffff;'>-</span></p>
<p>而布丁公主的攻擊方式就是一開始強制發動的「公主之歌」與正常的物理攻擊，公主之歌會讓全員進入狂暴狀態，說好不好，說差也不算什麼很糟糕的狀態，因為狂暴狀態的角色無法由玩家控制，只能強制重覆普通的物理攻擊，而好處則是攻擊力會因此提升 1.5 倍。</p>
<p><span style='color: #ffffff;'>-</span></p>
<p>偏偏布丁公主的防禦力也很高，一般的攻擊效果都不好，除非你已經取得了洋蔥之劍、飛龍之槍、龍鬚、村雨、正宗、月神之弓等等強力武器，且等級也升到差不多 92 ~ 95 級，那每次的傷害大概可以有個 1300 ~ 1800 左右，每隻布丁公主 HP 是 10000，這還是要砍蠻久的。</p>
<p><span style='color: #ffffff;'>-</span></p>
<p>以下提供幾種打布丁公主的方法，可以選擇最有效率的，或是選擇不花錢的，或者也可以一直重覆退出遊戲後重啟繼續的「大絕招」來不斷重新打，也是很有機會成功。( 通常會建議走完全地圖，取得傳繼能力道具「<span style='color: #800000;'>寶物獵手</span>」再來打，機率一定會較高的 )</p>
<p><span style='color: #ffffff;'>-</span></p>
<p><span style='color: #ffffff;'>-</span></p>
<p><strong>布丁公主打法建議：</strong></p>
<p><span style='color: #ffffff;'>-</span></p>
<p><span style='color: #0000ff;'><strong>( 一 ) 暴砍打法：</strong></span></p>
<p>( 效率普通，但不花錢也不費 MP，經濟型打法 )</p>
<p>1. 塞西爾裝備水晶套裝 ( 可以防狂暴狀態 )，技能裝後列斬、反擊即可 ( 再加<span style='color: #800000;'>寶物獵手</span>更好 )</p>
<p>2. 另外塞西爾如果有 HP + 50% 也可以考慮裝上，以等級 90~92 來刷的話，HP 大約在 7500 左右</p>
<p>3. 其他人一開始就會中「公主之歌」進入狂暴狀態，只會用物理攻擊，且不受控制</p>
<p>4. 所有人就裝備最好的攻擊武器，建議後面的終極武器都拿一拿再來打</p>
<p>5. 所有人的自動指令都設攻擊，開戰後就會自動開打了</p>
<p><span style='color: #ffffff;'>-</span></p>
<p><span style='color: #0000ff;'><strong>( 二 ) 星之沙打法：</strong></span>( 感謝網友 <span style='color: #0000ff;'>Double</span> 回覆正確的星之沙打法 )</p>
<p><span style='color: #ff0000;'>( 這是最有效率的打法，但是如果要一直連續打的話，要先花不少錢 )</span></p>
<p>1. 星之沙可以在地底世界的特美拉村買到，一個 $4800，一次買 70 個以上有打八折</p>
<p>2. 塞西爾的智力通常建議要在 30 以上，每丟一次大約可以造成三隻布丁公主各 3200 左右傷害</p>
<p>3. 塞西爾能力裝備：<span style='color: #800000;'>星之沙、反擊、誘敵、寶物獵手</span> ( 若有的話 )，方式如下圖排列：</p>
<p>( 自動指令就隨意了，不過因為要承受三次左右的攻擊，推薦自動指令選「<span style='color: #800000;'>防禦</span>」 )</p>
<p><img alt='' src='./ff4_images/ff4_part_3-2/img_9252.png' width='486' height='326' /></p>
<p><span style='color: #ffffff;'>-</span></p>
<p>如上圖將道具「星之沙」放在戰鬥指令的第一欄位置，這樣當塞西爾受到物理攻擊時，便會直接以「星之沙」來反擊，加上又裝了誘敵，等於三隻布丁公主會接連攻擊塞西爾，在三次的星之沙反擊後，順利的話直接解決這場戰鬥，就算沒解決，應該也各只差隨便再砍一次即可。( 這應該算是最有效率的打法了，如果錢不夠的話可以先買個 5~6 個星之沙，然後用退出遊戲後繼續的大絕招XD )</p>
<p>&nbsp;</p>
<p><span style='color: #ffffff;'>-</span></p>
<p><span style='color: #0000ff;'><strong>( 三 ) 解除狀態打法：</strong></span></p>
<p>( 成功機會較低，也打得比較慢一點，會重覆被丟公主之歌&#8230;.. )</p>
<p>1. 裝備同上述 ( 一 )，自動技能的部份塞西爾裝道具的「<span style='color: #800000;'>獨角獸之角</span>」( 可解除全體異常狀態 )</p>
<p>2. 莉迪亞自動技能：<span style='color: #800000;'>隕石</span> ( 或是<span style='color: #800000;'>核爆</span> )  ( 其他技能若有快速詠唱、魔法全體化當然會更好 )</p>
<p>3. 羅莎自動技能：<span style='color: #800000;'>神聖</span> ( 其他技能若有快速詠唱、魔法全體化當然會更好 )</p>
<p>4. 凱因自動技能：<span style='color: #800000;'>跳躍</span></p>
<p>5. 艾吉自動技能：<span style='color: #800000;'>攻擊</span></p>
<p>6. 開戰後除了塞西爾之外，全員一樣會中狂暴狀態，這時候塞西爾會丟獨角獸之角解除全體狀態</p>
<p>7. 之後要看大家自動的速度與運氣了，有機會在「公主之歌」再次發動前讓莉迪亞與羅莎出手</p>
<p>8. 當然解除了狀態之後，布丁公主還是會繼續放下一次公主之歌，還是會繼續中狂暴</p>
<p>9. 反正塞西爾就讓他一直丟獨角獸之角就好了沒差，因為塞西爾被攻擊還是會反擊的</p>
<p>10. 凱因成功跳上去之後，可以多一次較大的傷害 ( 雖然是單體而已，但是可以少砍很多下&#8230;. )</p>
<p><span style='color: #ffffff;'>-</span></p>
