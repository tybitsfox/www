<p><span style='color: #ffffff;'>-</span></p>
<p>FF4 的角色升級時，在 70 級之前所提升的數值都是固定的，不會受任何項目影響，但從第 71 級開始，所提升數值就會與當前身上所裝備的能力關聯。你可以讓每個角色學習許多能力，但是同時能裝在戰鬥指令欄位的只有 4 個，因為自動指令不影響升級，而道具這一項無法卸下。</p>
<p><span style='color: #ffffff;'>-</span></p>
<p>舉個例子，我在塞西爾要升第 71 級之前，讓他裝上了「<span style='line-height: 1.5em;'><span style='color: #800000;'>凝氣、對？的思念、金幣獵手、寶物獵手</span>」這 4 項，於是在升 71 級時提升狀況如下：</span></p>
<p><span style='line-height: 1.5em;'>HP <strong>+30</strong>、MP <strong>+5</strong>、力量 <strong>+3</strong>、敏捷 <strong>+3</strong>、體力智力精神各 <strong>+1</strong></span></p>
