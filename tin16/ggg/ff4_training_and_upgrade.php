<p><span style='line-height: 1.5em;'><span style='color: #ffffff;'>-</span></span></p>
<p><span style='line-height: 1.5em;'>上面的例子中，「</span><span style='color: #800000;'>凝氣</span><span style='line-height: 1.5em;'>」與「</span><span style='color: #800000;'>對？的思念</span><span style='line-height: 1.5em;'>」會各增加 HP 10 點，而總共增加了 30 點的原因應該是角色原本就會增加的 HP 值 ( 10 點也真少 )，不過不太確定，也可能是我技能放了哪幾個搞錯了，這部份等之後有空玩第三輪再來確認好了。</span></p>
<p><span style='color: #ffffff;'>-</span></p>
<p><span style='color: #ffffff;'>-</span></p>
<p>在 71 ~ 99 級裡面還有 29 級可以升，也就是說自由度其實還挺大的，在 HP / MP 的部份雖然提升很少，但是我們還是可以去刷<span style='color: #800000;'>金蘋果、銀蘋果、蘇摩之滴</span>來提升，所以可以專注在各能力值的培養上即可。</p>
