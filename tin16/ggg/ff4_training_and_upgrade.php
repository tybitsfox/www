<p>第一輪遊戲為了讓各角色能取得重要的傳繼能力道具，所以會比較難做最好選擇，但是第二輪就不同了，可以選擇使用的技能自由度很高，這時候就可以好好的思考一下，想讓你的角色學習哪一個技能？後期升級到了 70 級後，每一次的升級前就可以裝備相應的能力來加成數值。是會有點麻煩沒錯，因為要隨時注意經驗值是否快要達到升級標準，要在升級前替換能力。</p>
